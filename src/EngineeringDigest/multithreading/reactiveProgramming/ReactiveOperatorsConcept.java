/*

========================================================================
                REACTIVE OPERATORS
========================================================================

VERY IMPORTANT REACTIVE PROGRAMMING CONCEPT

========================================================================
WHY REACTIVE OPERATORS EXIST?
========================================================================

Reactive Programming works using:
========================================================================

data pipelines

========================================================================

Need:
ways to:
- transform data
- filter data
- combine data
- process async flow

========================================================================

This is why Reactive Operators exist.

========================================================================
WHAT ARE REACTIVE OPERATORS?
========================================================================

Reactive Operators are methods
used to process and transform
Mono/Flux data streams.

========================================================================
SIMPLE DEFINITION
========================================================================

Operators are pipeline steps
that manipulate reactive data flow.

========================================================================
MAIN IDEA
========================================================================

Data flows through:
multiple processing stages.

========================================================================
EXAMPLE FLOW
========================================================================

Flux<Data>
        ↓
map()
        ↓
filter()
        ↓
flatMap()
        ↓
Response

========================================================================
VERY IMPORTANT UNDERSTANDING
========================================================================

Reactive operators do NOT immediately execute.

========================================================================

They build:
========================================================================

Reactive Pipeline

========================================================================

Execution happens later during:
========================================================================

subscription

========================================================================

Advanced topic later.

========================================================================
MOST IMPORTANT OPERATORS
========================================================================

1. map()
2. flatMap()
3. filter()
4. collectList()
5. zip()

========================================================================
1. map()
========================================================================

Used for:
simple synchronous transformation.

========================================================================
EXAMPLE
========================================================================

String → Uppercase

========================================================================

User → UserDTO

========================================================================
MAIN IDEA
========================================================================

One input
        ↓
One transformed output

========================================================================
VERY IMPORTANT UNDERSTANDING
========================================================================

map() used for:
simple value transformation.

========================================================================
2. flatMap()
========================================================================

Used for:
asynchronous/reactive transformation.

========================================================================
EXAMPLE
========================================================================

User ID
        ↓
Async DB/API call
        ↓
Mono<User>

========================================================================
MAIN IDEA
========================================================================

Used when transformation itself
returns:
========================================================================

Mono
OR
Flux

========================================================================
VERY IMPORTANT INTERVIEW POINT
========================================================================

flatMap() flattens nested reactive publishers.

========================================================================
3. filter()
========================================================================

Used to:
filter unwanted data.

========================================================================
EXAMPLE
========================================================================

Only active users.

========================================================================

Only price > 1000.

========================================================================
MAIN IDEA
========================================================================

Keeps matching data only.

========================================================================
4. collectList()
========================================================================

Converts:
========================================================================

Flux<T>

========================================================================

into:
========================================================================

Mono<List<T>>

========================================================================

Useful when:
need complete collection together.

========================================================================
5. zip()
========================================================================

Combines multiple async results together.

========================================================================
EXAMPLE
========================================================================

User API result
        +
Order API result
        ↓
Combined response

========================================================================
VERY IMPORTANT UNDERSTANDING
========================================================================

Reactive operators create:
pipeline chain.

========================================================================

Each operator processes data step-by-step.

========================================================================
TRADITIONAL STYLE
========================================================================

Get data
        ↓
Modify manually
        ↓
Filter manually
        ↓
Return result

========================================================================
REACTIVE STYLE
========================================================================

Data stream
        ↓
map()
        ↓
filter()
        ↓
flatMap()
        ↓
Response

========================================================================

Much cleaner for async systems.

========================================================================
VERY IMPORTANT UNDERSTANDING
========================================================================

Operators usually work:
non-blockingly.

========================================================================

Designed for:
async pipelines.

========================================================================
COMMON PIPELINE EXAMPLE
========================================================================

Get Users
        ↓
filter(active users)
        ↓
map(User → DTO)
        ↓
flatMap(save async)
        ↓
Response

========================================================================
VERY IMPORTANT INTERVIEW POINT
========================================================================

Reactive Programming mainly revolves around:
========================================================================

operator pipelines

========================================================================

NOT manual thread handling.

========================================================================
REAL LIFE ANALOGY
========================================================================

Factory conveyor belt:
========================================================================

Raw material
        ↓
Cutting machine
        ↓
Filtering machine
        ↓
Packaging machine
        ↓
Final product

========================================================================

Operators work exactly like:
processing stages.

========================================================================
COMMON REAL-WORLD USE CASES
========================================================================

1. API transformations
2. Kafka stream processing
3. Event processing
4. Async DB pipelines
5. Notification systems

========================================================================
IMPORTANT ADVANTAGES
========================================================================

1. Cleaner async flow
2. Pipeline-based processing
3. Better scalability
4. Functional programming style
5. Easy data transformation

========================================================================
IMPORTANT LIMITATION
========================================================================

Complex operator chains
may become harder to debug.

========================================================================
REACTIVE OPERATORS SUMMARY
========================================================================

map()
--------
Simple transformation

========================================================================

flatMap()
------------
Async/reactive transformation

========================================================================

filter()
-----------
Filters data

========================================================================

collectList()
---------------
Flux → List conversion

========================================================================

zip()
--------
Combines publishers

========================================================================
IMPORTANT INTERVIEW QUESTIONS
========================================================================

1. What are Reactive Operators?

→ Methods used to process reactive data streams.

========================================================================

2. Which operator used for simple transformation?

→ map()

========================================================================

3. Which operator used for async transformation?

→ flatMap()

========================================================================

4. Which operator filters data?

→ filter()

========================================================================

5. Which operator combines publishers?

→ zip()

========================================================================

6. Difference between map and flatMap?

map()
--------
Simple transformation

flatMap()
------------
Reactive async transformation

========================================================================

MOST IMPORTANT INTERVIEW LINE

Reactive Operators are pipeline processing methods
used to transform, filter,
and combine reactive Mono/Flux streams asynchronously.

========================================================================

*/