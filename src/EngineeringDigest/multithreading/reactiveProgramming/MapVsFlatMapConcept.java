/*

========================================================================
                    map() vs flatMap()
========================================================================

MOST IMPORTANT REACTIVE INTERVIEW TOPIC

========================================================================
WHY THIS TOPIC IMPORTANT?
========================================================================

Almost every WebFlux/Reactor code uses:
========================================================================

map()
flatMap()

========================================================================

Understanding difference is CRITICAL.

========================================================================
FIRST UNDERSTAND map()
========================================================================

map() used for:
========================================================================

simple synchronous transformation

========================================================================
MAIN IDEA OF map()
========================================================================

One value
        ↓
One transformed value

========================================================================

Transformation immediate/simple.

========================================================================
EXAMPLE
========================================================================

User
        ↓
UserDTO

========================================================================

String
        ↓
Uppercase String

========================================================================

Number
        ↓
Square Number

========================================================================
VERY IMPORTANT UNDERSTANDING
========================================================================

map() returns:
========================================================================

normal transformed value

========================================================================

NOT another Mono/Flux.

========================================================================
MAP FLOW
========================================================================

Mono<User>
        ↓
map(user -> userDTO)
        ↓
Mono<UserDTO>

========================================================================

Simple transformation pipeline.

========================================================================
NOW UNDERSTAND flatMap()
========================================================================

flatMap() used for:
========================================================================

asynchronous/reactive transformation

========================================================================
MAIN IDEA OF flatMap()
========================================================================

One value
        ↓
Async operation
        ↓
Mono/Flux returned

========================================================================
EXAMPLE
========================================================================

UserId
        ↓
Database/API call
        ↓
Mono<User>

========================================================================

ProductId
        ↓
Service call
        ↓
Mono<Product>

========================================================================
VERY IMPORTANT UNDERSTANDING
========================================================================

flatMap() used when transformation itself returns:
========================================================================

Mono
OR
Flux

========================================================================
THIS IS MOST IMPORTANT POINT
========================================================================

map()
--------
Returns normal object/value.

========================================================================

flatMap()
------------
Returns reactive publisher
(Mono/Flux).

========================================================================
VERY IMPORTANT INTERVIEW POINT
========================================================================

flatMap() flattens nested reactive structures.

========================================================================
WHY flatMap NEEDED?
========================================================================

Without flatMap:
========================================================================

Mono<Mono<User>>

========================================================================

nested structure problem occurs.

========================================================================

flatMap() converts:
========================================================================

Mono<Mono<User>>
        ↓
Mono<User>

========================================================================
EXAMPLE FLOW OF map()
========================================================================

Mono<String>
        ↓
map(name -> name.toUpperCase())
        ↓
Mono<String>

========================================================================

Simple synchronous conversion.

========================================================================
EXAMPLE FLOW OF flatMap()
========================================================================

Mono<Integer>
        ↓
flatMap(id -> getUserFromDB(id))
        ↓
Mono<User>

========================================================================

Async/reactive operation.

========================================================================
VERY IMPORTANT UNDERSTANDING
========================================================================

Database/API calls mostly use:
========================================================================

flatMap()

========================================================================

Because:
they return Mono/Flux asynchronously.

========================================================================
WHEN TO USE map()?
========================================================================

Use when:
- simple transformation
- no async call
- no Mono/Flux returned

========================================================================
WHEN TO USE flatMap()?
========================================================================

Use when:
- async service call
- DB/API call
- reactive publisher returned

========================================================================
map vs flatMap CORE DIFFERENCE
========================================================================

map()
--------
Simple transformation

========================================================================

flatMap()
------------
Async/reactive transformation

========================================================================

map()
--------
Returns normal object

========================================================================

flatMap()
------------
Returns Mono/Flux

========================================================================

map()
--------
No flattening needed

========================================================================

flatMap()
------------
Flattens nested publishers

========================================================================
VERY IMPORTANT ENTERPRISE UNDERSTANDING
========================================================================

In WebFlux:
========================================================================

Most service calls use:
flatMap()

========================================================================

Most DTO transformations use:
map()

========================================================================
REAL LIFE ANALOGY
========================================================================

map()
--------
Simple packaging change.

========================================================================

Raw product
        ↓
Label changed

========================================================================

flatMap()
------------
Product sent to another factory
for async processing.

========================================================================

More complex pipeline.

========================================================================
COMMON REAL-WORLD USE CASES
========================================================================

map()
--------
- DTO conversion
- formatting
- calculations

========================================================================

flatMap()
------------
- DB calls
- API calls
- Kafka processing
- async service chaining

========================================================================
IMPORTANT LIMITATION
========================================================================

Overusing flatMap chains
may become difficult to debug.

========================================================================
INTERVIEW QUESTIONS
========================================================================

1. What is map()?

→ Operator for simple synchronous transformation.

========================================================================

2. What is flatMap()?

→ Operator for async/reactive transformation.

========================================================================

3. Main difference between map and flatMap?

map()
--------
Returns normal object

flatMap()
------------
Returns Mono/Flux

========================================================================

4. Why flatMap needed?

→ To flatten nested reactive publishers.

========================================================================

5. Which operator commonly used for DB/API calls?

→ flatMap()

========================================================================

6. Which operator commonly used for DTO conversion?

→ map()

========================================================================

MOST IMPORTANT INTERVIEW LINE

map() performs simple synchronous transformation,
whereas flatMap() handles asynchronous reactive transformations
that return Mono or Flux publishers.

========================================================================

*/