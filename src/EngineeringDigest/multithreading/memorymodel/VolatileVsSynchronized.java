/*

========================================================================
                VOLATILE vs SYNCHRONIZED
========================================================================

VERY IMPORTANT INTERVIEW TOPIC

========================================================================
WHY COMPARISON IMPORTANT?
========================================================================

Both:
- used in multithreading
- help with shared data
- provide visibility

========================================================================

BUT:
their purpose and power are different.

========================================================================
WHAT VOLATILE SOLVES?
========================================================================

volatile solves:
        visibility problem

========================================================================

Ensures:
latest value visible to all threads.

========================================================================
WHAT SYNCHRONIZED SOLVES?
========================================================================

synchronized solves:
- visibility problem
- atomicity problem
- race condition problem

========================================================================

Provides:
        mutual exclusion

========================================================================
MAIN DIFFERENCE
========================================================================

volatile
----------
Visibility only

========================================================================

synchronized
--------------
Visibility + atomicity

========================================================================
VERY IMPORTANT UNDERSTANDING
========================================================================

volatile does NOT lock anything.

========================================================================

synchronized uses:
        monitor lock

========================================================================
VOLATILE BEHAVIOR
========================================================================

Thread directly reads/writes
from main memory.

========================================================================

No locking happens.

========================================================================
SYNCHRONIZED BEHAVIOR
========================================================================

Only one thread enters critical section
at a time.

========================================================================

Other threads wait.

========================================================================
VISIBILITY
========================================================================

volatile
----------
YES

========================================================================

synchronized
--------------
YES

========================================================================
ATOMICITY
========================================================================

volatile
----------
NO

========================================================================

synchronized
--------------
YES

========================================================================
THREAD SAFETY
========================================================================

volatile
----------
Partial thread safety

========================================================================

synchronized
--------------
Full thread safety for critical section

========================================================================
PERFORMANCE
========================================================================

volatile
----------
Faster and lightweight

========================================================================

synchronized
--------------
Slower due to locking overhead

========================================================================
LOCKING
========================================================================

volatile
----------
No locking

========================================================================

synchronized
--------------
Uses locking

========================================================================
RACE CONDITION PROTECTION
========================================================================

volatile
----------
NO

========================================================================

synchronized
--------------
YES

========================================================================
IMPORTANT EXAMPLE
========================================================================

volatile int count = 0;

========================================================================

count++;

========================================================================

Still NOT thread-safe.

========================================================================

Because:
count++ is:
- read
- modify
- write

========================================================================

Multiple steps.

========================================================================

volatile cannot protect compound operations.

========================================================================
WHY SYNCHRONIZED SAFE HERE?
========================================================================

Because only one thread
can execute critical section at a time.

========================================================================
WHEN TO USE VOLATILE?
========================================================================

Use when:
- only visibility needed
- simple flag sharing
- no compound operations

========================================================================

Examples:
- shutdown flag
- status indicator

========================================================================
WHEN TO USE SYNCHRONIZED?
========================================================================

Use when:
- shared mutable data
- compound operations
- critical sections
- race condition possible

========================================================================

Examples:
- banking transactions
- counter increment
- inventory update

========================================================================
REAL LIFE ANALOGY
========================================================================

VOLATILE
----------
Everyone sees latest notice board.

========================================================================

SYNCHRONIZED
--------------
Only one person allowed inside room at a time.

========================================================================
VERY IMPORTANT INTERVIEW POINT
========================================================================

All synchronized blocks automatically provide:
        visibility guarantee

========================================================================

Because lock release flushes memory.

========================================================================

But volatile does NOT provide:
        mutual exclusion

========================================================================
VOLATILE vs SYNCHRONIZED SUMMARY
========================================================================

VOLATILE
----------
Visibility:
        YES

Atomicity:
        NO

Locking:
        NO

Performance:
        Faster

Best For:
        flags/status variables

========================================================================

SYNCHRONIZED
--------------
Visibility:
        YES

Atomicity:
        YES

Locking:
        YES

Performance:
        Slower

Best For:
        critical sections

========================================================================
MOST IMPORTANT UNDERSTANDING
========================================================================

volatile suitable for:
        simple shared state

========================================================================

synchronized suitable for:
        complex shared operations

========================================================================
IMPORTANT INTERVIEW QUESTIONS
========================================================================

1. Difference between volatile and synchronized?

volatile
----------
Visibility only

synchronized
--------------
Visibility + atomicity

========================================================================

2. Does volatile provide thread safety?

→ Only partial visibility safety.

========================================================================

3. Does synchronized provide visibility?

→ Yes

========================================================================

4. Which one uses locking?

→ synchronized

========================================================================

5. Which one is faster?

→ volatile

========================================================================

6. Is count++ safe with volatile?

→ No

========================================================================

7. Best use case of volatile?

→ Shared flags.

========================================================================

8. Best use case of synchronized?

→ Critical sections.

========================================================================

MOST IMPORTANT INTERVIEW LINE

volatile guarantees visibility,
whereas synchronized guarantees
both visibility and mutual exclusion.

========================================================================

*/