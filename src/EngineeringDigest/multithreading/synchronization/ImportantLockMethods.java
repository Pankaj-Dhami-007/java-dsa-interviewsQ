/*

========================================================================
                    IMPORTANT LOCK METHODS
========================================================================

Main class:
        ReentrantLock

Package:
        java.util.concurrent.locks

========================================================================

IMPORTANT METHODS

1. lock()
2. unlock()
3. tryLock()
4. tryLock(time, unit)
5. lockInterruptibly()
6. isLocked()
7. isHeldByCurrentThread()
8. getHoldCount()

========================================================================
1. lock()
========================================================================

        lock.lock();

PURPOSE
--------
Acquires lock.

========================================================================

IF LOCK AVAILABLE
------------------
Thread gets lock immediately.

========================================================================

IF LOCK NOT AVAILABLE
----------------------
Thread waits until lock released.

========================================================================

IMPORTANT
--------
Similar to:
        synchronized

========================================================================

EXAMPLE FLOW
========================================================================

Thread-1 acquires lock
        ↓
Thread-2 calls lock()
        ↓
Thread-2 waits

========================================================================

USE CASE
========================================================================

Used when thread MUST execute critical section.

========================================================================

IMPORTANT RULE
========================================================================

Always release using:
        unlock()

========================================================================
2. unlock()
========================================================================

        lock.unlock();

PURPOSE
--------
Releases acquired lock.

========================================================================

VERY IMPORTANT
========================================================================

ReentrantLock uses:
        MANUAL unlocking

========================================================================

If unlock() forgotten:
        deadlock may happen

========================================================================

BEST PRACTICE
========================================================================

Always use:
        finally block

========================================================================

EXAMPLE
========================================================================

try {

    lock.lock();

}
finally {

    lock.unlock();
}

========================================================================
3. tryLock()
========================================================================

        lock.tryLock();

PURPOSE
--------
Attempts to acquire lock immediately.

========================================================================

RETURNS
========================================================================

true
------
if lock acquired

false
-------
if lock unavailable

========================================================================

IMPORTANT
========================================================================

Does NOT wait forever.

========================================================================

BIG ADVANTAGE
========================================================================

Avoids unnecessary blocking.

========================================================================

USE CASE
========================================================================

High concurrency systems where waiting is expensive.

========================================================================

EXAMPLE FLOW
========================================================================

Thread-1 holds lock
        ↓
Thread-2 calls tryLock()
        ↓
returns false immediately

========================================================================
4. tryLock(time, unit)
========================================================================

        lock.tryLock(5, TimeUnit.SECONDS);

PURPOSE
--------
Waits for specific time to acquire lock.

========================================================================

IF LOCK AVAILABLE WITHIN TIME
------------------------------
returns true

========================================================================

OTHERWISE
----------
returns false

========================================================================

ADVANTAGE
========================================================================

Avoids infinite waiting.

========================================================================

USE CASE
========================================================================

Timeout-based systems.

========================================================================
5. lockInterruptibly()
========================================================================

        lock.lockInterruptibly();

PURPOSE
--------
Acquires lock but allows interruption while waiting.

========================================================================

NORMAL lock()
========================================================================

Thread waiting for lock
cannot be interrupted easily.

========================================================================

lockInterruptibly()
========================================================================

Waiting thread can be interrupted.

========================================================================

USE CASE
========================================================================

Responsive cancellation systems.

========================================================================

IMPORTANT
========================================================================

Throws:
        InterruptedException

========================================================================
6. isLocked()
========================================================================

        lock.isLocked();

PURPOSE
--------
Checks whether lock currently held by any thread.

========================================================================

RETURNS
========================================================================

true / false

========================================================================

USE CASE
========================================================================

Monitoring/debugging.

========================================================================
7. isHeldByCurrentThread()
========================================================================

        lock.isHeldByCurrentThread();

PURPOSE
--------
Checks whether current thread owns lock.

========================================================================

RETURNS
========================================================================

true / false

========================================================================

USE CASE
========================================================================

Safe unlocking validation.

========================================================================
8. getHoldCount()
========================================================================

        lock.getHoldCount();

PURPOSE
--------
Returns number of times current thread acquired lock.

========================================================================

IMPORTANT
========================================================================

ReentrantLock is:
        Reentrant

Same thread can acquire same lock multiple times.

========================================================================

EXAMPLE
========================================================================

lock.lock();
lock.lock();

Hold count:
        2

========================================================================
VERY IMPORTANT CONCEPT
========================================================================

REENTRANT LOCK
========================================================================

Same thread can re-enter same lock multiple times
without deadlock.

========================================================================

Thread must release lock equal number of times.

========================================================================
IMPORTANT DIFFERENCE
========================================================================

lock()
--------
Waits forever.

tryLock()
----------
Does not wait.

tryLock(time)
--------------
Waits limited time.

========================================================================
IMPORTANT DIFFERENCE
========================================================================

synchronized
--------------
Automatic lock handling

ReentrantLock
---------------
Manual lock handling

========================================================================
WHEN TO USE WHICH?
========================================================================

lock()
--------
When execution mandatory.

tryLock()
----------
When waiting should be avoided.

tryLock(timeout)
----------------
When limited waiting acceptable.

lockInterruptibly()
-------------------
When interruption support needed.

========================================================================
REAL INDUSTRY USE CASES
========================================================================

1. Banking systems
2. Payment gateways
3. Inventory management
4. High concurrency APIs
5. Thread pools
6. Scheduling systems

========================================================================
INTERVIEW QUESTIONS
========================================================================

1. Difference between lock() and tryLock()?

lock()
--------
waits indefinitely

tryLock()
----------
returns immediately

========================================================================

2. Why unlock() inside finally block?

→ Ensures lock release even during exception.

========================================================================

3. What is advantage of tryLock()?

→ Avoids unnecessary blocking.

========================================================================

4. Which method supports timeout locking?

→ tryLock(time, unit)

========================================================================

5. Which method allows interruption while waiting?

→ lockInterruptibly()

========================================================================

6. What does isLocked() check?

→ Whether lock held by any thread.

========================================================================

7. What is reentrant locking?

→ Same thread acquiring same lock multiple times.

========================================================================

8. Which package contains ReentrantLock?

→ java.util.concurrent.locks

========================================================================

9. Is ReentrantLock explicit or implicit lock?

→ Explicit lock.

========================================================================

10. Which is more flexible:
synchronized or ReentrantLock?

→ ReentrantLock

========================================================================

MOST IMPORTANT INTERVIEW LINE

ReentrantLock provides advanced synchronization
features like manual locking,
tryLock(), timeout locking,
and interruptible lock acquisition.

========================================================================

*/