/*

========================================================================
                        THREADLOCAL
========================================================================

ThreadLocal is VERY IMPORTANT
enterprise-level multithreading concept.

========================================================================
WHY THREADLOCAL EXISTS?
========================================================================

Normally:
threads share data.

========================================================================

Shared data may cause:
- race condition
- data corruption
- synchronization complexity

========================================================================

Sometimes requirement is different.

========================================================================

Need:
each thread should have
its OWN separate copy of data.

========================================================================

WITHOUT sharing.

========================================================================

This is why ThreadLocal exists.

========================================================================
WHAT IS THREADLOCAL?
========================================================================

ThreadLocal provides:
thread-specific separate storage.

========================================================================
SIMPLE DEFINITION
========================================================================

ThreadLocal creates separate variable copy
for each thread independently.

========================================================================
MAIN IDEA
========================================================================

Same variable name
        ↓
Different value per thread

========================================================================

Each thread gets:
its OWN private data.

========================================================================
VERY IMPORTANT UNDERSTANDING
========================================================================

ThreadLocal data is:
NOT shared between threads.

========================================================================

Every thread sees:
its own value only.

========================================================================
EXAMPLE SCENARIO
========================================================================

Suppose:
3 users hit backend server simultaneously.

========================================================================

Need separate:
- userId
- requestId
- transaction info
- security context

========================================================================

for every request thread.

========================================================================

ThreadLocal solves this perfectly.

========================================================================
IMPORTANT PACKAGE
========================================================================

        java.lang

========================================================================
IMPORTANT METHODS
========================================================================

1. set()
2. get()
3. remove()

========================================================================
1. set()
========================================================================

Stores value for current thread.

========================================================================
2. get()
========================================================================

Returns current thread's value.

========================================================================
3. remove()
========================================================================

Removes current thread's value.

========================================================================

VERY IMPORTANT in thread pools.

========================================================================
HOW THREADLOCAL WORKS?
========================================================================

Internally:
every Thread object contains:
========================================================================

ThreadLocalMap

========================================================================

Meaning:
thread stores its own local values.

========================================================================
VERY IMPORTANT UNDERSTANDING
========================================================================

ThreadLocal object itself shared.

========================================================================

BUT:
stored values separate per thread.

========================================================================
STEP-BY-STEP FLOW
========================================================================

Thread-1:
========================================================================

threadLocal.set("User-A")

========================================================================

Thread-2:
========================================================================

threadLocal.set("User-B")

========================================================================

Now:
========================================================================

Thread-1 get() → User-A

Thread-2 get() → User-B

========================================================================

No interference occurs.

========================================================================
WHY THREADLOCAL POWERFUL?
========================================================================

No synchronization needed.

========================================================================

Because:
data not shared.

========================================================================
THREADLOCAL vs NORMAL SHARED VARIABLE
========================================================================

NORMAL VARIABLE
----------------
Shared among threads

========================================================================

THREADLOCAL
-------------
Separate copy per thread

========================================================================

NORMAL VARIABLE
----------------
Synchronization required

========================================================================

THREADLOCAL
-------------
No synchronization required

========================================================================
VERY IMPORTANT INTERVIEW POINT
========================================================================

ThreadLocal provides:
        thread confinement

========================================================================

Meaning:
data confined to one thread only.

========================================================================
COMMON REAL-WORLD USE CASES
========================================================================

1. User session context
2. Spring Security Context
3. Database transaction context
4. Request tracing
5. Logging MDC
6. Hibernate Session

========================================================================
VERY IMPORTANT IN SPRING BOOT
========================================================================

Spring internally heavily uses:
        ThreadLocal

========================================================================

For:
- transactions
- request scope
- security context

========================================================================
IMPORTANT LIMITATION
========================================================================

ThreadLocal values may cause:
        memory leaks

========================================================================

Especially in:
thread pools

========================================================================

Because worker threads reused.

========================================================================
VERY IMPORTANT BEST PRACTICE
========================================================================

Always call:
========================================================================

remove()

========================================================================

after work completes.

========================================================================

Especially in:
ExecutorService/thread pools.

========================================================================
REAL LIFE ANALOGY
========================================================================

Suppose:
every employee has:
private locker.

========================================================================

Same locker design,
but separate contents.

========================================================================

Nobody shares locker items.

========================================================================

Exactly same concept.

========================================================================
VERY IMPORTANT INTERVIEW POINT
========================================================================

ThreadLocal does NOT solve:
        communication between threads.

========================================================================

It solves:
        data isolation per thread.

========================================================================
IMPORTANT ADVANTAGES
========================================================================

1. Thread isolation
2. No synchronization needed
3. Cleaner thread-specific data handling
4. Better enterprise context management

========================================================================
IMPORTANT LIMITATION
========================================================================

Improper cleanup may cause:
memory leaks in thread pools.

========================================================================
INTERVIEW QUESTIONS
========================================================================

1. What is ThreadLocal?

→ Provides thread-specific separate variable storage.

========================================================================

2. Why ThreadLocal used?

→ To maintain separate data per thread.

========================================================================

3. Is ThreadLocal shared between threads?

→ No

========================================================================

4. Which methods important in ThreadLocal?

→ set(), get(), remove()

========================================================================

5. Why remove() important?

→ Prevent memory leaks.

========================================================================

6. Does ThreadLocal require synchronization?

→ No

========================================================================

7. Real-world use case of ThreadLocal?

→ Request context, transaction management.

========================================================================

8. Which frameworks heavily use ThreadLocal?

→ Spring, Hibernate.

========================================================================

MOST IMPORTANT INTERVIEW LINE

ThreadLocal provides isolated thread-specific storage
where each thread maintains
its own independent copy of variable data.

========================================================================

*/