/*

========================================================================
                MULTITHREADING vs ASYNC
========================================================================

VERY IMPORTANT CONCEPT

Most beginners confuse:
- multithreading
- async programming

========================================================================
WHAT IS MULTITHREADING?
========================================================================

Using multiple threads
to execute tasks concurrently.

========================================================================

FOCUS
========================================================================

Execution using:
        multiple threads

========================================================================

QUESTION IT SOLVES
========================================================================

"Who executes task?"

========================================================================

ANSWER
========================================================================

Multiple worker threads.

========================================================================
EXAMPLE
========================================================================

Thread t1 = new Thread(task1);

Thread t2 = new Thread(task2);

========================================================================

Both threads execute tasks.

========================================================================
WHAT IS ASYNC?
========================================================================

Async means:
        asynchronous execution

========================================================================

Meaning:
start task and continue other work
without waiting immediately.

========================================================================

FOCUS
========================================================================

Non-blocking behavior.

========================================================================

QUESTION IT SOLVES
========================================================================

"Should thread wait?"

========================================================================

ANSWER
========================================================================

No immediate waiting.

========================================================================
SYNC vs ASYNC
========================================================================

SYNC
------
Do task
        ↓
wait for completion
        ↓
do next task

========================================================================

ASYNC
-------
Start task
        ↓
continue other work
        ↓
handle result later

========================================================================
REAL LIFE ANALOGY
========================================================================

SYNC
------
Order food and stand waiting.

========================================================================

ASYNC
-------
Order food and continue shopping.

========================================================================
VERY IMPORTANT
========================================================================

Multithreading and Async are:
        RELATED
BUT NOT SAME.

========================================================================
MULTITHREADING FOCUS
========================================================================

Thread execution.

========================================================================
ASYNC FOCUS
========================================================================

Waiting behavior.

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Async often uses:
        multithreading internally

========================================================================

Because background tasks still need:
worker threads.

========================================================================
MULTITHREADING WITHOUT ASYNC
========================================================================

Possible.

========================================================================

Example:
========================================================================

t1.start();

t1.join();

========================================================================

Multiple threads exist,
BUT main thread waits.

========================================================================

So:
multithreading exists
but async behavior absent.

========================================================================
ASYNC WITHOUT MULTITHREADING
========================================================================

Possible.

========================================================================

Example:
- Node.js event loop
- reactive systems

========================================================================

Single-thread async systems exist.

========================================================================
EXECUTORSERVICE ROLE
========================================================================

ExecutorService manages:
- worker threads
- thread pools
- task execution
- thread lifecycle

========================================================================

FOCUS
========================================================================

Thread management.

========================================================================
COMPLETABLEFUTURE ROLE
========================================================================

CompletableFuture manages:
- async workflows
- callbacks
- task chaining
- non-blocking result processing

========================================================================

FOCUS
========================================================================

Async flow management.

========================================================================
VERY IMPORTANT DIFFERENCE
========================================================================

ExecutorService
----------------
"Which thread executes task?"

========================================================================

CompletableFuture
------------------
"What happens after task completion?"

========================================================================
RELATIONSHIP
========================================================================

CompletableFuture often uses:
- ExecutorService
OR
- ForkJoinPool internally

========================================================================

Meaning:
CompletableFuture still needs threads.

========================================================================
SIMPLE FLOW
========================================================================

ExecutorService
----------------
Task
        ↓
Worker Thread
        ↓
Execution

========================================================================

CompletableFuture
------------------
Async Task
        ↓
Result Ready
        ↓
Automatic Callback
        ↓
Next Async Step

========================================================================
MOST IMPORTANT LINE
========================================================================

Multithreading is about:
        execution

========================================================================

Async programming is about:
        non-blocking waiting behavior

========================================================================
INTERVIEW QUESTIONS
========================================================================

1. Difference between multithreading and async?

Multithreading
---------------
Multiple thread execution

Async
-------
Non-blocking execution flow

========================================================================

2. Does async always require multithreading?

→ No

========================================================================

3. Does Java async usually use threads internally?

→ Yes

========================================================================

4. What ExecutorService manages?

→ Threads and task execution.

========================================================================

5. What CompletableFuture manages?

→ Async workflows and callbacks.

========================================================================

6. Can multithreading exist without async?

→ Yes

========================================================================

7. Main focus of async programming?

→ Avoid blocking while waiting.

========================================================================

MOST IMPORTANT INTERVIEW LINE

Multithreading focuses on concurrent execution,
whereas async programming focuses on non-blocking workflow handling.

========================================================================

*/