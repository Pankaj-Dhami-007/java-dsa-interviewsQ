/*

========================================================================
                        CALLABLE
========================================================================

Callable is one of the MOST IMPORTANT concepts
in Executor Framework.

========================================================================
WHY CALLABLE EXISTS?
========================================================================

Runnable had limitations.

========================================================================
LIMITATIONS OF RUNNABLE
========================================================================

1. Cannot return result
2. Cannot throw checked exception

========================================================================

Example:
========================================================================

Runnable task = () -> {

    return 10; // NOT ALLOWED

};

========================================================================

Sometimes tasks need:
- return values
- calculations
- database results
- API responses

========================================================================

Runnable cannot do this.

========================================================================
THIS IS WHY CALLABLE EXISTS
========================================================================

Callable introduced to:
- return result
- throw checked exception

========================================================================
WHAT IS CALLABLE?
========================================================================

Callable is functional interface
representing task that:
- returns value
- may throw exception

========================================================================
IMPORTANT PACKAGE
========================================================================

        java.util.concurrent

========================================================================
IMPORTANT METHOD
========================================================================

        call()

========================================================================

Defined as:
========================================================================

V call() throws Exception

========================================================================

V means:
generic return type.

========================================================================
VERY IMPORTANT UNDERSTANDING
========================================================================

Runnable uses:
        run()

========================================================================

Callable uses:
        call()

========================================================================
MAIN DIFFERENCE
========================================================================

Runnable
----------
No return value

========================================================================

Callable
----------
Returns value

========================================================================
IMPORTANT DIFFERENCE
========================================================================

Runnable
----------
Cannot throw checked exception

========================================================================

Callable
----------
Can throw checked exception

========================================================================
VERY IMPORTANT UNDERSTANDING
========================================================================

Callable does NOT work directly with:
        Thread class

========================================================================

Callable mainly used with:
        ExecutorService

========================================================================
WHY?
========================================================================

Because ExecutorService supports:
        submit()

========================================================================

submit() returns:
        Future

========================================================================

Future later retrieves Callable result.

========================================================================
SIMPLE FLOW
========================================================================

Callable task submitted
        ↓
ExecutorService executes task
        ↓
Future object returned
        ↓
Result retrieved later

========================================================================
REAL LIFE ANALOGY
========================================================================

Runnable
----------
Worker does work silently.

========================================================================

Callable
----------
Worker does work and returns report/result.

========================================================================
COMMON USE CASES
========================================================================

1. Database query result
2. API response
3. File processing result
4. Mathematical calculations
5. Async computations

========================================================================
IMPORTANT ADVANTAGES
========================================================================

1. Supports return value
2. Supports checked exception
3. Better async programming
4. Works with Future

========================================================================
IMPORTANT LIMITATION
========================================================================

Cannot directly run using:
        Thread.start()

========================================================================

Must use:
        ExecutorService

========================================================================
CALLABLE vs RUNNABLE
========================================================================

RUNNABLE
----------
run()

========================================================================

CALLABLE
----------
call()

========================================================================

RUNNABLE
----------
No return value

========================================================================

CALLABLE
----------
Returns value

========================================================================

RUNNABLE
----------
No checked exception

========================================================================

CALLABLE
----------
Checked exception supported

========================================================================

RUNNABLE
----------
Thread compatible

========================================================================

CALLABLE
----------
ExecutorService compatible

========================================================================
IMPORTANT GENERIC CONCEPT
========================================================================

Callable<Integer>
Callable<String>
Callable<Double>

========================================================================

Type specifies:
        return type

========================================================================
WHY CALLABLE IMPORTANT IN MODERN JAVA?
========================================================================

Modern applications often require:
- async results
- background computations
- task tracking

========================================================================

Callable + Future solve this.

========================================================================
IMPORTANT INTERVIEW POINT
========================================================================

Callable is:
        Functional Interface

========================================================================

Because:
only one abstract method exists.

========================================================================

So lambda expressions supported.

========================================================================
EXAMPLE STYLE
========================================================================

Callable<Integer> task = () -> {

    return 100;
};

========================================================================
REAL-WORLD USE CASES
========================================================================

1. Banking systems
2. API aggregation
3. Async calculations
4. Data processing
5. Microservices

========================================================================
INTERVIEW QUESTIONS
========================================================================

1. Why Callable introduced?

→ Runnable cannot return result or throw checked exception.

========================================================================

2. Main method of Callable?

→ call()

========================================================================

3. Does Callable return value?

→ Yes

========================================================================

4. Can Callable throw checked exception?

→ Yes

========================================================================

5. Which package contains Callable?

→ java.util.concurrent

========================================================================

6. Can Callable work directly with Thread class?

→ No

========================================================================

7. Which ExecutorService method supports Callable?

→ submit()

========================================================================

8. Which object retrieves Callable result?

→ Future

========================================================================

9. Is Callable functional interface?

→ Yes

========================================================================

10. Main difference between Runnable and Callable?

Runnable
----------
No result

Callable
----------
Returns result

========================================================================

MOST IMPORTANT INTERVIEW LINE

Callable is advanced task interface
that supports returning results
and throwing checked exceptions
during asynchronous execution.

========================================================================

*/