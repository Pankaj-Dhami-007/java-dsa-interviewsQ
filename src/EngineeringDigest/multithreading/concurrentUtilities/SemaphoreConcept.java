/*

========================================================================
                            SEMAPHORE
========================================================================

Semaphore is VERY IMPORTANT synchronization utility
used to control:
        limited resource access.

========================================================================
WHY SEMAPHORE EXISTS?
========================================================================

Sometimes resource is limited.

========================================================================

Examples:
- database connections
- parking slots
- printers
- API rate limits

========================================================================

Need:
only limited number of threads
should access resource simultaneously.

========================================================================

synchronized allows:
ONLY ONE thread at a time.

========================================================================

BUT sometimes we need:
- 2 threads
- 5 threads
- 10 threads

========================================================================

This is why Semaphore exists.

========================================================================
WHAT IS SEMAPHORE?
========================================================================

Semaphore controls number of threads
that can access resource simultaneously.

========================================================================
SIMPLE DEFINITION
========================================================================

Semaphore is permit-based synchronization mechanism.

========================================================================
MAIN IDEA
========================================================================

Limited permits available.

========================================================================

Thread must acquire permit
before accessing resource.

========================================================================

After work:
thread releases permit.

========================================================================
IMPORTANT PACKAGE
========================================================================

        java.util.concurrent

========================================================================
IMPORTANT METHODS
========================================================================

1. acquire()
2. release()
3. tryAcquire()

========================================================================
HOW SEMAPHORE WORKS?
========================================================================

Suppose:
========================================================================

Semaphore(3)

========================================================================

Meaning:
3 permits available.

========================================================================

At most:
3 threads can access resource simultaneously.

========================================================================
STEP-BY-STEP FLOW
========================================================================

Thread requests permit
        ↓
If permit available:
access granted

========================================================================

If no permit available:
thread waits.

========================================================================

When thread finishes:
release() called
        ↓
permit returned back.

========================================================================

Waiting thread may proceed.

========================================================================
VERY IMPORTANT UNDERSTANDING
========================================================================

Semaphore controls:
        quantity of concurrent access

========================================================================

NOT mutual exclusion only.

========================================================================
SEMAPHORE TYPES
========================================================================

1. Binary Semaphore
2. Counting Semaphore

========================================================================
1. BINARY SEMAPHORE
========================================================================

Only 1 permit.

========================================================================

Works similar to:
        mutex/lock

========================================================================
2. COUNTING SEMAPHORE
========================================================================

Multiple permits.

========================================================================

Example:
========================================================================

Semaphore(5)

========================================================================

Allows:
5 concurrent threads.

========================================================================
VERY IMPORTANT INTERVIEW POINT
========================================================================

Semaphore may allow:
multiple threads simultaneously.

========================================================================

synchronized allows:
only one thread.

========================================================================
SEMAPHORE vs SYNCHRONIZED
========================================================================

synchronized
--------------
One thread only

========================================================================

Semaphore
-----------
Limited multiple threads possible

========================================================================

synchronized
--------------
Lock-based

========================================================================

Semaphore
-----------
Permit-based

========================================================================
IMPORTANT METHODS
========================================================================

1. acquire()
========================================================================

Acquires permit.

========================================================================

If permit unavailable:
thread waits.

========================================================================
2. release()
========================================================================

Returns permit back.

========================================================================

Allows waiting threads to continue.

========================================================================
3. tryAcquire()
========================================================================

Attempts permit acquisition
without blocking.

========================================================================

Returns:
- true
- false

========================================================================
VERY IMPORTANT UNDERSTANDING
========================================================================

If thread forgets:
        release()

========================================================================

Permit leak occurs.

========================================================================

Other threads may wait forever.

========================================================================
REAL LIFE ANALOGY
========================================================================

Parking lot with:
        3 parking spaces

========================================================================

Only 3 cars can park simultaneously.

========================================================================

If full:
new cars wait outside.

========================================================================

When one car leaves:
space becomes available.

========================================================================

Exactly same concept.

========================================================================
COMMON REAL-WORLD USE CASES
========================================================================

1. Database connection pool
2. API rate limiting
3. Printer access
4. Thread throttling
5. Resource pools

========================================================================
IMPORTANT ADVANTAGES
========================================================================

1. Controls concurrent access
2. Better resource management
3. Prevents overload
4. Supports multiple concurrent threads

========================================================================
IMPORTANT LIMITATION
========================================================================

Improper release handling
may cause:
- deadlock
- permit leakage

========================================================================
VERY IMPORTANT INTERVIEW POINT
========================================================================

Semaphore does NOT track ownership.

========================================================================

Any thread can:
        release permit

========================================================================

Unlike:
        ReentrantLock

========================================================================
INTERVIEW QUESTIONS
========================================================================

1. What is Semaphore?

→ Permit-based synchronization utility.

========================================================================

2. Why Semaphore used?

→ To control limited concurrent resource access.

========================================================================

3. Difference between Semaphore and synchronized?

synchronized
--------------
One thread only

Semaphore
-----------
Multiple threads possible

========================================================================

4. Which package contains Semaphore?

→ java.util.concurrent

========================================================================

5. Which method acquires permit?

→ acquire()

========================================================================

6. Which method releases permit?

→ release()

========================================================================

7. What happens if no permit available?

→ Thread waits.

========================================================================

8. What is Binary Semaphore?

→ Semaphore with one permit.

========================================================================

9. Main use case of Semaphore?

→ Resource pool management.

========================================================================

MOST IMPORTANT INTERVIEW LINE

Semaphore controls limited concurrent access
to shared resources
using permit-based synchronization.

========================================================================

*/