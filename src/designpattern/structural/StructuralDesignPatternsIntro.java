package designpattern.structural;

public class StructuralDesignPatternsIntro {

    /*
        ==========================================================
        STRUCTURAL DESIGN PATTERNS
        ==========================================================

        We completed:

            CREATIONAL DESIGN PATTERNS

        Their focus was:

            OBJECT CREATION

        ----------------------------------------------------------

        Now new category:

            STRUCTURAL DESIGN PATTERNS

     */



    /*
        ==========================================================
        MAIN GOAL
        ==========================================================

        Structural patterns focus on:

            HOW CLASSES AND OBJECTS
            ARE CONNECTED TOGETHER

     */



    /*
        ==========================================================
        SIMPLE UNDERSTANDING
        ==========================================================

        Creational Patterns:
            how objects are CREATED

        Structural Patterns:
            how objects are ORGANIZED

     */



    /*
        ==========================================================
        REAL LIFE ANALOGY
        ==========================================================

        Imagine building house.

        ----------------------------------------------------------

        CREATIONAL:
            creating bricks, doors, windows

        ----------------------------------------------------------

        STRUCTURAL:
            arranging them properly
            to build house structure

     */



    /*
        ==========================================================
        MAIN PURPOSE
        ==========================================================

        Structural patterns help:

        - reduce complexity
        - improve flexibility
        - improve maintainability
        - connect incompatible systems
        - extend behavior cleanly

     */



    /*
        ==========================================================
        TYPES OF STRUCTURAL PATTERNS
        ==========================================================

        1. Adapter

        2. Decorator

        3. Facade

        4. Proxy

        5. Composite

        6. Bridge

        7. Flyweight

     */



    /*
        ==========================================================
        QUICK UNDERSTANDING
        ==========================================================

        Adapter:
            converts incompatible interfaces

        Decorator:
            adds behavior dynamically

        Facade:
            simplified interface

        Proxy:
            controlled access

        Composite:
            tree structure objects

        Bridge:
            separates abstraction from implementation

        Flyweight:
            memory optimization by sharing objects

     */



    /*
        ==========================================================
        MOST IMPORTANT PATTERNS IN INDUSTRY
        ==========================================================

        VERY heavily used:

        - Adapter
        - Decorator
        - Proxy
        - Facade

     */



    /*
        ==========================================================
        SPRING FRAMEWORK USAGE
        ==========================================================

        Spring heavily uses:

        - Proxy
        - Decorator
        - Adapter

        Example:
        Spring AOP internally uses Proxy pattern.

     */



    /*
        ==========================================================
        REAL COMPANY USAGE
        ==========================================================

        Netflix:
            Proxy + Facade

        Amazon:
            Adapter integrations

        Payment Gateways:
            Adapter pattern

        Logging frameworks:
            Decorator

        Security frameworks:
            Proxy

     */



    /*
        ==========================================================
        IMPORTANT UNDERSTANDING
        ==========================================================

        Structural patterns mostly focus on:

        - composition
        - wrapping
        - delegation
        - abstraction

     */



    /*
        ==========================================================
        COMPOSITION OVER INHERITANCE
        ==========================================================

        Structural patterns heavily prefer:

            HAS-A relationship

        instead of:

            IS-A relationship

     */



    /*
        ==========================================================
        WHY?
        ==========================================================

        Composition gives:
        - flexibility
        - runtime behavior changes
        - loose coupling

     */



    /*
        ==========================================================
        INTERVIEW QUESTIONS
        ==========================================================

        Q1:
        Main goal of structural patterns?

        Answer:
        Organize classes and objects efficiently.



        Q2:
        Difference between creational and structural?

        Answer:

        Creational:
        object creation

        Structural:
        object organization



        Q3:
        Most used structural patterns?

        Answer:
        Adapter, Decorator, Proxy, Facade.



        Q4:
        Which principle heavily used?

        Answer:
        Composition over inheritance.



        Q5:
        Why structural patterns important?

        Answer:
        Flexible and maintainable architecture.

     */



    /*
        ==========================================================
        IS STRUCTURAL INTRO COMPLETE?
        ==========================================================

        YES.

        Next:
            Adapter Design Pattern

        MOST IMPORTANT first structural pattern.

     */



    public static void main(String[] args) {

        System.out.println(
                "Structural Design Patterns Started"
        );

    }

}