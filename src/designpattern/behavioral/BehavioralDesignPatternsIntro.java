package designpattern.behavioral;

public class BehavioralDesignPatternsIntro {

    /*
        ==========================================================
        BEHAVIORAL DESIGN PATTERNS
        ==========================================================

        We already completed:

        1. Creational Design Patterns
        2. Structural Design Patterns

        ----------------------------------------------------------

        Now third major category:

            Behavioral Design Patterns

     */



    /*
        ==========================================================
        MAIN GOAL
        ==========================================================

        Behavioral patterns focus on:

            HOW OBJECTS COMMUNICATE
            AND BEHAVE WITH EACH OTHER

     */



    /*
        ==========================================================
        SIMPLE UNDERSTANDING
        ==========================================================

        Creational:
            object creation

        Structural:
            object organization

        Behavioral:
            object communication and behavior

     */



    /*
        ==========================================================
        REAL LIFE ANALOGY
        ==========================================================

        Imagine company office.

        ----------------------------------------------------------

        Creational:
            hiring employees

        Structural:
            organizing departments

        Behavioral:
            how employees communicate/work

     */



    /*
        ==========================================================
        MAIN PURPOSE
        ==========================================================

        Behavioral patterns help:

        - flexible communication
        - loose coupling
        - runtime behavior changes
        - request processing
        - responsibility delegation

     */



    /*
        ==========================================================
        MOST IMPORTANT BEHAVIORAL PATTERNS
        ==========================================================

        VERY heavily used in industry:

        1. Strategy

        2. Observer

        3. Command

        4. Chain Of Responsibility

        5. Template Method

     */



    /*
        ==========================================================
        QUICK UNDERSTANDING
        ==========================================================

        Strategy:
            interchangeable algorithms

        Observer:
            event notification system

        Command:
            encapsulate requests

        Chain Of Responsibility:
            request processing chain

        Template Method:
            fixed workflow with custom steps

     */



    /*
        ==========================================================
        MOST USED IN REAL PROJECTS
        ==========================================================

        Strategy:
            payment systems

        Observer:
            Kafka, event systems

        Command:
            undo-redo systems

        Chain:
            Spring Security filters

        Template Method:
            framework workflows

     */



    /*
        ==========================================================
        SPRING FRAMEWORK USAGE
        ==========================================================

        Spring heavily uses:
        - Strategy
        - Chain Of Responsibility
        - Template Method
        - Observer concepts

     */



    /*
        ==========================================================
        IMPORTANT CONCEPT
        ==========================================================

        Behavioral patterns mainly focus on:

        - delegation
        - runtime behavior
        - communication
        - dynamic workflow

     */



    /*
        ==========================================================
        INTERVIEW QUESTIONS
        ==========================================================

        Q1:
        Main goal of behavioral patterns?

        Answer:
        Object communication and behavior management.



        Q2:
        Difference from structural patterns?

        Answer:

        Structural:
        object organization

        Behavioral:
        object interaction/communication



        Q3:
        Most used behavioral pattern?

        Answer:
        Strategy Pattern.



        Q4:
        Which behavioral pattern used in events?

        Answer:
        Observer Pattern.



        Q5:
        Which pattern used in Spring Security?

        Answer:
        Chain Of Responsibility.

     */



    /*
        ==========================================================
        IS BEHAVIORAL INTRO COMPLETE?
        ==========================================================

        YES.

        Next:
            Strategy Design Pattern

        MOST IMPORTANT behavioral pattern.

     */



    public static void main(String[] args) {

        System.out.println(
                "Behavioral Design Patterns Started"
        );

    }

}