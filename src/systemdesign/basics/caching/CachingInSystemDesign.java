/*

========================================================================
                    CACHING IN SYSTEM DESIGN
========================================================================

Caching is one of MOST IMPORTANT
concepts in system design.

========================================================================

Almost every large-scale application uses caching.

========================================================================
EXAMPLES
========================================================================

- Netflix
- Instagram
- YouTube
- Amazon
- Facebook

========================================================================

Without caching,
modern internet would become VERY slow.

========================================================================
MAIN QUESTION
========================================================================

Why caching needed?

========================================================================
PROBLEM
========================================================================

Suppose 10 million users request same data.

========================================================================

Example:
Instagram profile of celebrity.

========================================================================

Without cache:

Every request goes to:
        database

========================================================================

PROBLEMS
========================================================================

1. Database overload
2. Slow response
3. High latency
4. Server crash possibility
5. Poor scalability

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Database access is expensive.

========================================================================

Fetching data repeatedly from database
is inefficient.

========================================================================
THIS IS WHY
========================================================================

Caching exists.

========================================================================
WHAT IS CACHE?
========================================================================

Cache is temporary high-speed storage
used to store frequently accessed data.

========================================================================
SIMPLE IDEA
========================================================================

Instead of fetching data repeatedly
from database:

Store frequently used data
in faster storage.

========================================================================
FLOW WITHOUT CACHE
========================================================================

Client
   ↓
Application Server
   ↓
Database
   ↓
Response

========================================================================

Every request hits database.

========================================================================
FLOW WITH CACHE
========================================================================

Client
   ↓
Application Server
   ↓
Cache
   ↓
Database (only if cache miss)

========================================================================
IMPORTANT GOAL OF CACHE
========================================================================

Reduce:
- database load
- latency
- response time

========================================================================

Improve:
- performance
- scalability
- throughput

========================================================================
REAL WORLD EXAMPLE
========================================================================

Suppose:
Instagram profile data accessed
millions of times.

========================================================================

Instead of querying database repeatedly:

Store profile data in cache.

========================================================================

Future requests directly use cache.

========================================================================
IMPORTANT TERMINOLOGIES
========================================================================

1. Cache Hit
2. Cache Miss

========================================================================
1. CACHE HIT
========================================================================

Requested data found in cache.

========================================================================

Fast response returned.

========================================================================

NO database query needed.

========================================================================
2. CACHE MISS
========================================================================

Requested data NOT found in cache.

========================================================================

Application fetches from database.

========================================================================

Then stores result into cache.

========================================================================
CACHE HIT FLOW
========================================================================

Client
   ↓
Server
   ↓
Cache Found
   ↓
Response

========================================================================
CACHE MISS FLOW
========================================================================

Client
   ↓
Server
   ↓
Cache Not Found
   ↓
Database Query
   ↓
Store in Cache
   ↓
Response

========================================================================
WHY CACHE FAST?
========================================================================

Because cache usually stores data:
- in memory (RAM)
- not on disk

========================================================================

RAM is MUCH faster than disk storage.

========================================================================
COMMON CACHE TECHNOLOGIES
========================================================================

- :contentReference[oaicite:0]{index=0}
- :contentReference[oaicite:1]{index=1}

========================================================================
REDIS
========================================================================

Most popular caching technology.

========================================================================

Redis stores data:
        in memory

========================================================================

Provides:
- extremely fast reads
- extremely fast writes

========================================================================
IMPORTANT REAL WORLD USAGE
========================================================================

Redis used for:
- caching
- sessions
- rate limiting
- pub/sub
- leaderboards

========================================================================
WHAT TYPE OF DATA SHOULD BE CACHED?
========================================================================

Good candidates:
- frequently accessed data
- rarely changing data
- expensive queries

========================================================================
EXAMPLES
========================================================================

1. User profiles
2. Product details
3. Trending videos
4. Feed data
5. Configuration data

========================================================================
WHAT SHOULD NOT BE CACHED?
========================================================================

1. Highly dynamic data
2. Sensitive real-time data
3. Rapidly changing values

========================================================================
EXAMPLE
========================================================================

Bank account balance.

========================================================================

Because stale cache can cause
incorrect results.

========================================================================
CACHE EXPIRATION
========================================================================

Cache should not live forever.

========================================================================

Old data becomes:
        stale

========================================================================
THIS IS WHY
========================================================================

TTL used.

========================================================================
WHAT IS TTL?
========================================================================

TTL =
        Time To Live

========================================================================

Defines how long cache remains valid.

========================================================================
EXAMPLE
========================================================================

Cache user profile for:
        10 minutes

========================================================================

After expiration:
cache removed automatically.

========================================================================
CACHE EVICTION
========================================================================

When cache memory becomes full,
some data must be removed.

========================================================================
THIS IS CALLED
========================================================================

Cache eviction.

========================================================================
COMMON EVICTION POLICIES
========================================================================

1. LRU
2. LFU
3. FIFO

========================================================================
1. LRU
========================================================================

Least Recently Used.

========================================================================

Removes data not used recently.

========================================================================
2. LFU
========================================================================

Least Frequently Used.

========================================================================

Removes least accessed data.

========================================================================
3. FIFO
========================================================================

First In First Out.

========================================================================

Oldest cache removed first.

========================================================================
CACHE ASIDE PATTERN
========================================================================

Most common caching strategy.

========================================================================
FLOW
========================================================================

1. Application checks cache
2. If found → return
3. Else query database
4. Store in cache
5. Return response

========================================================================
VERY IMPORTANT SYSTEM DESIGN CONCEPT
========================================================================

Cache improves:
        READ performance

========================================================================

But introduces:
        consistency challenges

========================================================================
CACHE CONSISTENCY PROBLEM
========================================================================

Suppose:
database updated

========================================================================

But cache still contains old data.

========================================================================

This creates:
        stale data problem

========================================================================
SOLUTIONS
========================================================================

1. Cache invalidation
2. Short TTL
3. Write-through cache
4. Event-based updates

========================================================================
CACHE INVALIDATION
========================================================================

When database updates:
remove/update cache immediately.

========================================================================
VERY IMPORTANT QUOTE
========================================================================

"There are only two hard things
in Computer Science:

1. Cache invalidation
2. Naming things"

========================================================================
DISTRIBUTED CACHE
========================================================================

Large systems use:
        distributed caching

========================================================================

Meaning:
cache shared across multiple servers.

========================================================================
WHY DISTRIBUTED CACHE?
========================================================================

Because:
multiple backend servers exist.

========================================================================

Each server should access
same cached data.

========================================================================
REAL WORLD FLOW
========================================================================

Users
   ↓
Load Balancer
   ↓
Multiple Application Servers
   ↓
Shared Redis Cache
   ↓
Database

========================================================================
IMPORTANT SYSTEM DESIGN UNDERSTANDING
========================================================================

Cache reduces database traffic massively.

========================================================================

Without cache,
many large systems would fail.

========================================================================
CACHE USE CASES
========================================================================

1. API responses
2. User sessions
3. Authentication tokens
4. Trending content
5. Product catalog
6. Search results

========================================================================
WHEN CACHE CAN BE DANGEROUS?
========================================================================

1. Stale data
2. Incorrect invalidation
3. Memory overflow
4. Cache stampede

========================================================================
WHAT IS CACHE STAMPEDE?
========================================================================

Suppose:
popular cache expires suddenly.

========================================================================

Millions of requests now hit database together.

========================================================================

Database overload occurs.

========================================================================
SOLUTIONS
========================================================================

1. Staggered expiration
2. Background refresh
3. Distributed locking

========================================================================
IMPORTANT INTERVIEW QUESTIONS
========================================================================

1. What is caching?

→ Temporary high-speed storage
for frequently accessed data.

========================================================================

2. Why caching needed?

→ Reduce database load and improve performance.

========================================================================

3. Difference between cache hit and miss?

Hit
-----
data found in cache

Miss
------
data not found in cache

========================================================================

4. Why Redis popular?

→ Extremely fast in-memory storage.

========================================================================

5. What is TTL?

→ Time duration before cache expires.

========================================================================

6. What is cache eviction?

→ Removing data when cache becomes full.

========================================================================

7. What is cache consistency problem?

→ Cache contains outdated data.

========================================================================

8. What is distributed cache?

→ Shared cache across multiple servers.

========================================================================

9. What is cache stampede?

→ Huge traffic hitting database
after cache expiration.

========================================================================
MOST IMPORTANT INTERVIEW LINE
========================================================================

Caching improves system performance
by storing frequently accessed data
in fast temporary storage,
reducing database load and latency.

========================================================================

*/