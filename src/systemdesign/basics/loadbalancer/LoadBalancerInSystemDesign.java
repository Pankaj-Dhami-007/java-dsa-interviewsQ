/*

========================================================================
                LOAD BALANCER IN SYSTEM DESIGN
========================================================================

Load Balancer is one of MOST IMPORTANT
scaling concepts in system design.

========================================================================

Almost every high-traffic application uses:
        Load Balancer

========================================================================
EXAMPLES
========================================================================

- Netflix
- Instagram
- Amazon
- WhatsApp
- YouTube

========================================================================
MAIN QUESTION
========================================================================

Why Load Balancer needed?

========================================================================
PROBLEM
========================================================================

Suppose:
single backend server handles all traffic.

========================================================================

Initially:
100 users

========================================================================

Server works fine.

========================================================================

Later:
10 million users arrive.

========================================================================

PROBLEMS
========================================================================

1. Server overload
2. High CPU usage
3. Memory exhaustion
4. Slow responses
5. Server crash

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Single server cannot handle
infinite traffic.

========================================================================

This creates:
        Scalability Problem

========================================================================
SOLUTION
========================================================================

Add multiple servers.

========================================================================
EXAMPLE
========================================================================

Instead of:

        1 server

Use:

        10 servers

========================================================================
NEW PROBLEM
========================================================================

How users distributed
across multiple servers?

========================================================================

THIS IS WHY
========================================================================

Load Balancer exists.

========================================================================
WHAT IS LOAD BALANCER?
========================================================================

Load Balancer distributes incoming requests
across multiple backend servers.

========================================================================
MAIN GOAL
========================================================================

1. Prevent server overload
2. Improve scalability
3. Improve availability
4. Improve fault tolerance
5. Improve performance

========================================================================
SIMPLE FLOW
========================================================================

Users
   ↓
Load Balancer
   ↓
Multiple Servers

========================================================================
REAL WORLD FLOW
========================================================================

Clients
   ↓
Load Balancer
   ↓
Server-1
Server-2
Server-3

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Client usually does NOT know:
which actual server handles request.

========================================================================

Client only communicates with:
        Load Balancer

========================================================================
LOAD BALANCER RESPONSIBILITIES
========================================================================

1. Request distribution
2. Health checking
3. Failure handling
4. Traffic management
5. SSL termination sometimes

========================================================================
HEALTH CHECK
========================================================================

Load Balancer continuously checks:
whether servers are alive.

========================================================================

If server crashes:
Load Balancer stops sending traffic to it.

========================================================================
THIS IMPROVES
========================================================================

High availability.

========================================================================
IMPORTANT SYSTEM DESIGN UNDERSTANDING
========================================================================

Load Balancer removes:
        single point of failure

========================================================================

Because traffic distributed
across multiple servers.

========================================================================
COMMON LOAD BALANCING ALGORITHMS
========================================================================

1. Round Robin
2. Least Connections
3. IP Hash
4. Weighted Round Robin

========================================================================
1. ROUND ROBIN
========================================================================

Requests distributed sequentially.

========================================================================
EXAMPLE
========================================================================

Request-1 → Server-1
Request-2 → Server-2
Request-3 → Server-3
Request-4 → Server-1

========================================================================

Most basic algorithm.

========================================================================
ADVANTAGE
========================================================================

Simple and fast.

========================================================================
DISADVANTAGE
========================================================================

Does not consider:
server load.

========================================================================
2. LEAST CONNECTIONS
========================================================================

Request sent to server
with least active connections.

========================================================================
ADVANTAGE
========================================================================

Better traffic balancing.

========================================================================
USEFUL FOR
========================================================================

Long-running requests.

========================================================================
3. IP HASH
========================================================================

Server selected using:
client IP hash.

========================================================================
USE CASE
========================================================================

Session persistence.

========================================================================
4. WEIGHTED ROUND ROBIN
========================================================================

More powerful servers receive:
more traffic.

========================================================================
EXAMPLE
========================================================================

Server-1 → Weight 5
Server-2 → Weight 2

========================================================================

Server-1 receives more requests.

========================================================================
TYPES OF LOAD BALANCERS
========================================================================

1. Hardware Load Balancer
2. Software Load Balancer

========================================================================
1. HARDWARE LOAD BALANCER
========================================================================

Physical devices.

========================================================================

Very expensive.

========================================================================

Used in traditional enterprise systems.

========================================================================
2. SOFTWARE LOAD BALANCER
========================================================================

Software-based solutions.

========================================================================
EXAMPLES
========================================================================

- :contentReference[oaicite:0]{index=0}
- :contentReference[oaicite:1]{index=1}

========================================================================

Most modern systems use:
        software load balancers

========================================================================
LAYER 4 LOAD BALANCER
========================================================================

Works at:
        Transport Layer

========================================================================

Uses:
- IP
- TCP/UDP ports

========================================================================

Very fast.

========================================================================
LAYER 7 LOAD BALANCER
========================================================================

Works at:
        Application Layer

========================================================================

Can inspect:
- URLs
- headers
- cookies

========================================================================
EXAMPLE
========================================================================

/images → Image Server

/api → API Server

========================================================================
VERY IMPORTANT UNDERSTANDING
========================================================================

Layer 7 provides:
        intelligent routing

========================================================================

But slightly slower than Layer 4.

========================================================================
LOAD BALANCER + SCALABILITY
========================================================================

Without Load Balancer:
scaling impossible efficiently.

========================================================================

With Load Balancer:
new servers added dynamically.

========================================================================
EXAMPLE
========================================================================

Traffic increases:
        Add more servers

========================================================================

Load Balancer automatically distributes traffic.

========================================================================
AUTO SCALING
========================================================================

Cloud platforms can:
automatically create servers
during high traffic.

========================================================================

Load Balancer routes traffic
to new servers automatically.

========================================================================
SESSION STICKINESS
========================================================================

Sometimes requests from same user
must go to same server.

========================================================================

This called:
        Sticky Sessions

========================================================================
PROBLEM
========================================================================

If server crashes:
user session lost.

========================================================================

Modern systems avoid this using:
- Redis
- Distributed sessions
- JWT

========================================================================
IMPORTANT REAL WORLD ARCHITECTURE
========================================================================

Users
   ↓
CDN
   ↓
Load Balancer
   ↓
Application Servers
   ↓
Redis Cache
   ↓
Database

========================================================================
FAILOVER
========================================================================

Suppose:
Server-2 crashes.

========================================================================

Load Balancer detects failure
through health checks.

========================================================================

Traffic redirected to:
- Server-1
- Server-3

========================================================================

Application continues running.

========================================================================
THIS PROVIDES
========================================================================

Fault tolerance.

========================================================================
COMMON USE CASES
========================================================================

1. High traffic websites
2. Microservices
3. Cloud systems
4. APIs
5. Video streaming platforms

========================================================================
IMPORTANT SYSTEM DESIGN UNDERSTANDING
========================================================================

Load Balancer mainly helps:
        Horizontal Scaling

========================================================================
WHAT IS HORIZONTAL SCALING?
========================================================================

Adding more servers
instead of increasing single server power.

========================================================================
VERTICAL vs HORIZONTAL SCALING
========================================================================

Vertical Scaling
-----------------
Increase CPU/RAM of same server.

========================================================================

Horizontal Scaling
-------------------
Add more servers.

========================================================================

Modern distributed systems prefer:
        Horizontal Scaling

========================================================================
WHY?
========================================================================

Because vertical scaling has:
hardware limits.

========================================================================
COMMON PROBLEMS WITH LOAD BALANCERS
========================================================================

1. Single point of failure
2. Uneven traffic distribution
3. Sticky session issues
4. Health check delays

========================================================================
SOLUTION
========================================================================

Use:
- multiple load balancers
- distributed architecture
- stateless servers

========================================================================
IMPORTANT INTERVIEW QUESTIONS
========================================================================

1. What is Load Balancer?

→ Component that distributes traffic
across multiple servers.

========================================================================

2. Why Load Balancer needed?

→ Prevent overload and improve scalability.

========================================================================

3. What is Round Robin?

→ Sequential request distribution algorithm.

========================================================================

4. Difference between Layer 4 and Layer 7?

Layer 4
--------
works using TCP/IP

Layer 7
--------
works using HTTP/application data

========================================================================

5. What is health check?

→ Checking whether server is alive.

========================================================================

6. What is sticky session?

→ Same user routed to same server.

========================================================================

7. Why horizontal scaling preferred?

→ Better scalability and fault tolerance.

========================================================================

8. What happens if server crashes?

→ Load Balancer redirects traffic
to healthy servers.

========================================================================
MOST IMPORTANT INTERVIEW LINE
========================================================================

Load Balancer distributes incoming traffic
across multiple servers
to improve scalability,
availability,
and fault tolerance.

========================================================================

*/