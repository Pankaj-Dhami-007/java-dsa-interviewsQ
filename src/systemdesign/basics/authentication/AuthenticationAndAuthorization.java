/*

========================================================================
        AUTHENTICATION AND AUTHORIZATION
========================================================================

Authentication and Authorization
are MOST IMPORTANT security concepts
in backend systems.

========================================================================

Almost every application uses:
- login systems
- user verification
- access control

========================================================================
EXAMPLES
========================================================================

- :contentReference[oaicite:0]{index=0}
- :contentReference[oaicite:1]{index=1}
- :contentReference[oaicite:2]{index=2}
- :contentReference[oaicite:3]{index=3}

========================================================================
MAIN QUESTION
========================================================================

How systems identify users
and control access securely?

========================================================================
THIS LEADS TO
========================================================================

1. Authentication
2. Authorization

========================================================================
1. AUTHENTICATION
========================================================================

Authentication means:
        verifying identity

========================================================================
SIMPLE IDEA
========================================================================

Who are you?

========================================================================
EXAMPLE
========================================================================

User enters:
- email
- password

========================================================================

System verifies credentials.

========================================================================

If correct:
user authenticated.

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Authentication proves:
user identity.

========================================================================
REAL WORLD EXAMPLES
========================================================================

1. Login system
2. OTP verification
3. Biometric login
4. Google Sign-In

========================================================================
2. AUTHORIZATION
========================================================================

Authorization means:
        checking permissions

========================================================================
SIMPLE IDEA
========================================================================

What are you allowed to do?

========================================================================
EXAMPLE
========================================================================

Admin user can:
- delete users
- access admin APIs

========================================================================

Normal user cannot.

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Authentication happens first.

========================================================================

Authorization happens after authentication.

========================================================================
FLOW
========================================================================

User Login
   ↓
Authentication
   ↓
Authorization
   ↓
Access Granted/Denied

========================================================================
IMPORTANT INTERVIEW POINT
========================================================================

Authentication
----------------
identity verification

========================================================================

Authorization
---------------
permission verification

========================================================================
LOGIN FLOW
========================================================================

User
   ↓
Enter Credentials
   ↓
Backend Server
   ↓
Database Verification
   ↓
Token/Session Generated
   ↓
Access Granted

========================================================================
PASSWORD STORAGE
========================================================================

Passwords should NEVER be stored
in plain text.

========================================================================
VERY IMPORTANT SECURITY RULE
========================================================================

Always store:
        hashed passwords

========================================================================
WHAT IS HASHING?
========================================================================

Converting password into:
fixed encrypted-like value.

========================================================================

Hashing is:
one-way operation.

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Original password cannot be recovered easily.

========================================================================
POPULAR HASHING ALGORITHMS
========================================================================

- BCrypt
- Argon2
- PBKDF2

========================================================================
BAD PRACTICE
========================================================================

Storing plain passwords.

========================================================================

Major security vulnerability.

========================================================================
SALT
========================================================================

Random value added before hashing.

========================================================================
WHY SALT IMPORTANT?
========================================================================

Prevents:
rainbow table attacks.

========================================================================
SESSION-BASED AUTHENTICATION
========================================================================

Traditional authentication mechanism.

========================================================================
FLOW
========================================================================

User logs in
   ↓
Server creates session
   ↓
Session ID stored
   ↓
Client sends Session ID
in future requests

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Server maintains:
session state.

========================================================================
PROBLEM
========================================================================

Scaling becomes difficult
in distributed systems.

========================================================================

Because:
sessions stored in server memory.

========================================================================
THIS LEADS TO
========================================================================

Sticky sessions problem.

========================================================================
JWT AUTHENTICATION
========================================================================

Modern scalable systems prefer:
        JWT

========================================================================
WHAT IS JWT?
========================================================================

JWT =
        JSON Web Token

========================================================================

Self-contained authentication token.

========================================================================
FLOW
========================================================================

User logs in
   ↓
Server generates JWT
   ↓
Client stores token
   ↓
Future requests contain JWT

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Server does NOT need
session storage.

========================================================================

This supports:
stateless authentication.

========================================================================
ADVANTAGES OF JWT
========================================================================

1. Stateless
2. Scalable
3. Microservices-friendly
4. No server session memory needed

========================================================================
JWT STRUCTURE
========================================================================

1. Header
2. Payload
3. Signature

========================================================================
HEADER
========================================================================

Contains:
token type and algorithm.

========================================================================
PAYLOAD
========================================================================

Contains:
user information/claims.

========================================================================
EXAMPLE
========================================================================

{
    "userId": 101,
    "role": "ADMIN"
}

========================================================================
SIGNATURE
========================================================================

Prevents token tampering.

========================================================================
IMPORTANT SECURITY UNDERSTANDING
========================================================================

JWT payload should NOT contain:
sensitive data.

========================================================================

Because payload can be decoded.

========================================================================
ACCESS TOKEN
========================================================================

Short-lived authentication token.

========================================================================
REFRESH TOKEN
========================================================================

Used to generate new access tokens.

========================================================================
WHY REFRESH TOKEN NEEDED?
========================================================================

Improves security.

========================================================================

Access tokens expire quickly.

========================================================================
AUTHENTICATION METHODS
========================================================================

1. Password Authentication
2. OTP Authentication
3. OAuth
4. SSO
5. Biometric Authentication

========================================================================
OAUTH
========================================================================

Delegated authorization framework.

========================================================================
EXAMPLE
========================================================================

"Login with Google"

========================================================================

Application does NOT store
Google password.

========================================================================
SSO
========================================================================

Single Sign-On.

========================================================================

One login works across:
multiple applications.

========================================================================
EXAMPLE
========================================================================

Google account accessing:
- Gmail
- YouTube
- Drive

========================================================================
MULTI-FACTOR AUTHENTICATION (MFA)
========================================================================

Multiple verification methods used.

========================================================================
EXAMPLE
========================================================================

Password + OTP

========================================================================
ADVANTAGE
========================================================================

Improved security.

========================================================================
AUTHORIZATION MODELS
========================================================================

1. Role-Based Access Control (RBAC)
2. Attribute-Based Access Control (ABAC)

========================================================================
RBAC
========================================================================

Permissions based on:
roles.

========================================================================
EXAMPLE
========================================================================

ADMIN
USER
MODERATOR

========================================================================
ABAC
========================================================================

Permissions based on:
attributes and conditions.

========================================================================
EXAMPLE
========================================================================

Access allowed only during:
office hours.

========================================================================
IMPORTANT SYSTEM DESIGN UNDERSTANDING
========================================================================

Authentication systems must support:
- scalability
- security
- fault tolerance
- distributed environments

========================================================================
AUTHENTICATION IN MICROSERVICES
========================================================================

Usually handled by:
- API Gateway
- Authentication Service

========================================================================
FLOW
========================================================================

Client
   ↓
API Gateway
   ↓
JWT Validation
   ↓
Microservices

========================================================================
WHY CENTRALIZED AUTH IMPORTANT?
========================================================================

Avoids repeating:
authentication logic
inside every service.

========================================================================
COMMON SECURITY THREATS
========================================================================

1. Brute force attacks
2. Token theft
3. Session hijacking
4. Credential stuffing
5. Replay attacks

========================================================================
SECURITY BEST PRACTICES
========================================================================

1. HTTPS everywhere
2. Password hashing
3. Token expiration
4. MFA
5. Rate limiting
6. Secure cookies

========================================================================
COOKIES
========================================================================

Small data stored in browser.

========================================================================

Often used for:
session management.

========================================================================
HTTP ONLY COOKIE
========================================================================

JavaScript cannot access cookie.

========================================================================

Improves security.

========================================================================
REAL WORLD ARCHITECTURE
========================================================================

Client
   ↓
API Gateway
   ↓
Authentication Service
   ↓
JWT Validation
   ↓
Microservices

========================================================================
IMPORTANT INTERVIEW QUESTIONS
========================================================================

1. Difference between authentication and authorization?

Authentication
----------------
who are you?

Authorization
---------------
what can you access?

========================================================================

2. Why passwords hashed?

→ Prevent password exposure.

========================================================================

3. What is JWT?

→ Stateless authentication token.

========================================================================

4. Why JWT scalable?

→ No server-side session storage needed.

========================================================================

5. What is OAuth?

→ Delegated authorization framework.

========================================================================

6. What is MFA?

→ Multiple verification methods.

========================================================================

7. Why HTTPS important in authentication?

→ Protect credentials and tokens.

========================================================================

8. What is RBAC?

→ Role-based permission system.

========================================================================

9. Why access tokens short-lived?

→ Improve security.

========================================================================
MOST IMPORTANT INTERVIEW LINE
========================================================================

Authentication verifies user identity
while authorization determines
what resources and operations
the authenticated user can access.

========================================================================

*/