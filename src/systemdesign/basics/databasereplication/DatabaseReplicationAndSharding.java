/*

========================================================================
        DATABASE REPLICATION AND SHARDING
========================================================================

Replication and Sharding are
MOST IMPORTANT database scaling techniques
in large-scale systems.

========================================================================

Modern applications cannot scale forever
using single database server.

========================================================================
EXAMPLES
========================================================================

- :contentReference[oaicite:0]{index=0}
- :contentReference[oaicite:1]{index=1}
- :contentReference[oaicite:2]{index=2}
- :contentReference[oaicite:3]{index=3}

========================================================================
MAIN QUESTION
========================================================================

What happens when:
single database cannot handle traffic?

========================================================================
PROBLEMS
========================================================================

1. Slow queries
2. High CPU usage
3. Memory bottleneck
4. Too many connections
5. Storage limits

========================================================================

Eventually:
database becomes bottleneck.

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Scaling database is MUCH HARDER
than scaling application servers.

========================================================================

Because:
data consistency becomes difficult.

========================================================================
THIS IS WHY
========================================================================

Replication and Sharding exist.

========================================================================
DATABASE REPLICATION
========================================================================

Replication means:
copying same database data
to multiple servers.

========================================================================
SIMPLE IDEA
========================================================================

Instead of:
one database server

========================================================================

Use:
multiple copies of database.

========================================================================
MAIN GOALS
========================================================================

1. High availability
2. Fault tolerance
3. Read scaling
4. Backup support

========================================================================
REPLICATION FLOW
========================================================================

Primary Database
      ↓
Replica-1
Replica-2
Replica-3

========================================================================
PRIMARY DATABASE
========================================================================

Main database handling:
- writes
- updates
- deletes

========================================================================
REPLICAS
========================================================================

Copy databases mainly handling:
        read operations

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Reads distributed across replicas.

========================================================================

This reduces load
on primary database.

========================================================================
REAL WORLD EXAMPLE
========================================================================

Instagram users viewing profiles.

========================================================================

Millions of reads occur.

========================================================================

Read replicas handle:
majority read traffic.

========================================================================
THIS IMPROVES
========================================================================

1. Scalability
2. Performance
3. Availability

========================================================================
REPLICATION TYPES
========================================================================

1. Synchronous Replication
2. Asynchronous Replication

========================================================================
1. SYNCHRONOUS REPLICATION
========================================================================

Primary waits until replicas updated.

========================================================================

Write completes only after:
all replicas synchronized.

========================================================================
ADVANTAGE
========================================================================

Strong consistency.

========================================================================
DISADVANTAGE
========================================================================

Slower performance.

========================================================================
USE CASE
========================================================================

Banking systems.

========================================================================
2. ASYNCHRONOUS REPLICATION
========================================================================

Primary updates first.

========================================================================

Replicas update later.

========================================================================
ADVANTAGE
========================================================================

Better performance and availability.

========================================================================
DISADVANTAGE
========================================================================

Temporary stale data possible.

========================================================================
USE CASE
========================================================================

Social media systems.

========================================================================
REPLICATION LAG
========================================================================

Delay between:
primary update
and replica synchronization.

========================================================================
PROBLEM
========================================================================

Users may see:
old data temporarily.

========================================================================
EXAMPLE
========================================================================

Instagram like count delay.

========================================================================
FAILOVER
========================================================================

Suppose:
primary database crashes.

========================================================================

Replica promoted as:
new primary.

========================================================================

This improves:
fault tolerance.

========================================================================
IMPORTANT SYSTEM DESIGN UNDERSTANDING
========================================================================

Replication mainly helps:
        read scaling

========================================================================

NOT massive write scaling.

========================================================================
SHARDING
========================================================================

Sharding means:
splitting database data
across multiple servers.

========================================================================
SIMPLE IDEA
========================================================================

Instead of:
one huge database

========================================================================

Use:
multiple smaller databases.

========================================================================
MAIN GOAL
========================================================================

Handle massive data and write traffic.

========================================================================
SHARDING FLOW
========================================================================

Users 1-1M
    ↓
Shard-1

========================================================================

Users 1M-2M
    ↓
Shard-2

========================================================================

Users 2M-3M
    ↓
Shard-3

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Each shard stores:
different subset of data.

========================================================================

Unlike replication:
where all replicas store same data.

========================================================================
REPLICATION vs SHARDING
========================================================================

REPLICATION
-------------
Same data copied.

========================================================================

SHARDING
----------
Different data distributed.

========================================================================
WHY SHARDING IMPORTANT?
========================================================================

Single database cannot handle:
- billions of users
- petabytes of data
- massive write traffic

========================================================================
SHARDING STRATEGIES
========================================================================

1. Range-based Sharding
2. Hash-based Sharding
3. Geographic Sharding

========================================================================
1. RANGE-BASED SHARDING
========================================================================

Data split using ranges.

========================================================================
EXAMPLE
========================================================================

Users 1-1000 → Shard-1

Users 1001-2000 → Shard-2

========================================================================
PROBLEM
========================================================================

Uneven traffic possible.

========================================================================
2. HASH-BASED SHARDING
========================================================================

Shard selected using:
hash function.

========================================================================
ADVANTAGE
========================================================================

Better data distribution.

========================================================================
3. GEOGRAPHIC SHARDING
========================================================================

Data divided based on:
geographical regions.

========================================================================
EXAMPLE
========================================================================

India users → India DB

US users → US DB

========================================================================
ADVANTAGE
========================================================================

Lower latency.

========================================================================
SHARDING CHALLENGES
========================================================================

1. Complex queries
2. Cross-shard joins
3. Rebalancing
4. Distributed transactions
5. Data migration

========================================================================
CROSS-SHARD QUERY
========================================================================

Data required from:
multiple shards.

========================================================================

Very expensive and complex.

========================================================================
REBALANCING
========================================================================

Suppose:
Shard-1 becomes overloaded.

========================================================================

Need redistribution of data.

========================================================================

Complex operation.

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Sharding increases:
operational complexity massively.

========================================================================
COMBINING REPLICATION + SHARDING
========================================================================

Large-scale systems use:
both techniques together.

========================================================================
EXAMPLE
========================================================================

Shard-1
   ↓
Replica-1
Replica-2

========================================================================

Shard-2
   ↓
Replica-1
Replica-2

========================================================================
THIS PROVIDES
========================================================================

1. Write scaling
2. Read scaling
3. High availability
4. Fault tolerance

========================================================================
IMPORTANT SYSTEM DESIGN UNDERSTANDING
========================================================================

Replication solves:
read scalability.

========================================================================

Sharding solves:
write scalability and storage scaling.

========================================================================
REAL WORLD ARCHITECTURE
========================================================================

Users
   ↓
Application Servers
   ↓
Shard Router
   ↓
Multiple Database Shards
   ↓
Read Replicas

========================================================================
SHARD ROUTER
========================================================================

Determines:
which shard contains data.

========================================================================
EXAMPLE
========================================================================

User ID → hash(userId)
        ↓
Select shard

========================================================================
EVENTUAL CONSISTENCY
========================================================================

Large distributed databases often use:
eventual consistency.

========================================================================

Especially with:
asynchronous replication.

========================================================================
DATABASE CLUSTER
========================================================================

Group of databases
working together.

========================================================================

Clusters improve:
- scalability
- reliability
- availability

========================================================================
IMPORTANT INTERVIEW QUESTIONS
========================================================================

1. What is replication?

→ Copying same data across multiple databases.

========================================================================

2. Why replication used?

→ High availability and read scaling.

========================================================================

3. What is sharding?

→ Splitting data across multiple databases.

========================================================================

4. Difference between replication and sharding?

Replication
-------------
same data copies

Sharding
----------
different data distribution

========================================================================

5. What is replication lag?

→ Delay between primary and replica synchronization.

========================================================================

6. What is failover?

→ Switching replica as primary during failure.

========================================================================

7. Why sharding difficult?

→ Distributed query and coordination complexity.

========================================================================

8. Which solves read scaling?

→ Replication

========================================================================

9. Which solves write scaling?

→ Sharding

========================================================================
MOST IMPORTANT INTERVIEW LINE
========================================================================

Replication improves read scalability
and availability
while sharding improves write scalability
by distributing data across multiple databases.

========================================================================

*/