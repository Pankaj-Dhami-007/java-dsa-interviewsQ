/*

========================================================================
                DESIGN TINYURL / URL SHORTENER
========================================================================

URL Shortener is one of MOST POPULAR
system design interview questions.

========================================================================

Companies ask this because it covers:
- database design
- scalability
- caching
- unique ID generation
- distributed systems basics

========================================================================
EXAMPLES
========================================================================

- :contentReference[oaicite:0]{index=0}
- :contentReference[oaicite:1]{index=1}

========================================================================
PROBLEM STATEMENT
========================================================================

Convert:
very long URL

========================================================================

Into:
small short URL

========================================================================
EXAMPLE
========================================================================

LONG URL
========================================================================

https://example.com/products/mobile/iphone/very-long-url

========================================================================

SHORT URL
========================================================================

https://tinyurl.com/Ab12X

========================================================================
MAIN GOALS
========================================================================

1. Short unique URLs
2. Fast redirection
3. Scalability
4. High availability
5. Low latency

========================================================================
STEP 1 : REQUIREMENTS
========================================================================

FUNCTIONAL REQUIREMENTS
========================================================================

1. Generate short URL
2. Redirect short URL
3. URL expiration optional
4. Analytics optional

========================================================================
NON-FUNCTIONAL REQUIREMENTS
========================================================================

1. High availability
2. Fast redirection
3. Massive scalability
4. Fault tolerance

========================================================================
STEP 2 : CAPACITY ESTIMATION
========================================================================

Suppose:
100 million URLs/day

========================================================================

Average URL size:
500 bytes

========================================================================

Storage estimation becomes important.

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Read traffic much higher than writes.

========================================================================

Because:
redirects happen frequently.

========================================================================
READ HEAVY SYSTEM
========================================================================

Write:
URL creation

========================================================================

Read:
URL redirection

========================================================================

Redirections massively higher.

========================================================================
STEP 3 : HIGH LEVEL FLOW
========================================================================

SHORT URL CREATION
========================================================================

Client
   ↓
API Server
   ↓
Generate Unique ID
   ↓
Store Mapping
   ↓
Return Short URL

========================================================================
REDIRECTION FLOW
========================================================================

Client clicks short URL
   ↓
Load Balancer
   ↓
Application Server
   ↓
Cache Lookup
   ↓
Database Lookup (if needed)
   ↓
Redirect to Original URL

========================================================================
STEP 4 : DATABASE DESIGN
========================================================================

Need mapping:
ShortCode → OriginalURL

========================================================================
TABLE DESIGN
========================================================================

short_urls
------------
id
short_code
original_url
created_at
expiration_time

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

short_code must be:
unique.

========================================================================
STEP 5 : UNIQUE ID GENERATION
========================================================================

MOST IMPORTANT part.

========================================================================
QUESTION
========================================================================

How generate unique short codes?

========================================================================
COMMON APPROACHES
========================================================================

1. Auto Increment ID
2. Hashing
3. Random Generation
4. Distributed ID Generation

========================================================================
1. AUTO INCREMENT ID
========================================================================

Database generates:
1, 2, 3, 4...

========================================================================

Then encode into:
Base62.

========================================================================
WHAT IS BASE62?
========================================================================

Uses characters:
[a-z][A-Z][0-9]

========================================================================

Total:
62 characters.

========================================================================
EXAMPLE
========================================================================

125 → "cb"

========================================================================
ADVANTAGE
========================================================================

Simple and compact.

========================================================================
DISADVANTAGE
========================================================================

Sequential IDs predictable.

========================================================================
2. HASHING
========================================================================

Hash long URL.

========================================================================

Take first few characters.

========================================================================
PROBLEM
========================================================================

Collision possibility.

========================================================================
WHAT IS COLLISION?
========================================================================

Different URLs generating
same short code.

========================================================================
SOLUTION
========================================================================

Collision detection and retry.

========================================================================
3. RANDOM GENERATION
========================================================================

Generate random Base62 strings.

========================================================================
PROBLEM
========================================================================

Need uniqueness checking.

========================================================================
STEP 6 : BASE62 ENCODING
========================================================================

Very common interview topic.

========================================================================

Why Base62?

========================================================================

Because:
large values represented
using fewer characters.

========================================================================
CHARACTERS
========================================================================

a-z = 26

A-Z = 26

0-9 = 10

========================================================================

Total:
62 characters.

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Shorter URLs improve:
usability.

========================================================================
STEP 7 : CACHING
========================================================================

Redirection requests very frequent.

========================================================================

Need cache for:
ShortCode → OriginalURL

========================================================================
FLOW
========================================================================

User clicks short URL
   ↓
Redis Cache
   ↓
Database (if cache miss)

========================================================================
WHY CACHE IMPORTANT?
========================================================================

Reduces:
- database load
- latency

========================================================================
COMMON TECHNOLOGY
========================================================================

- :contentReference[oaicite:2]{index=2}

========================================================================
STEP 8 : DATABASE SCALING
========================================================================

Large systems require:
replication and sharding.

========================================================================
REPLICATION
========================================================================

Improves:
- read scalability
- availability

========================================================================
SHARDING
========================================================================

Splits data across:
multiple databases.

========================================================================
STEP 9 : LOAD BALANCING
========================================================================

Traffic distributed across:
multiple application servers.

========================================================================
FLOW
========================================================================

Users
   ↓
Load Balancer
   ↓
Application Servers

========================================================================
STEP 10 : CDN
========================================================================

Popular redirects may use:
CDN caching.

========================================================================

Improves global redirection speed.

========================================================================
REDIRECTION RESPONSE
========================================================================

Use HTTP:
301 or 302 redirect.

========================================================================
301
========================================================================

Permanent redirect.

========================================================================
302
========================================================================

Temporary redirect.

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Browsers automatically redirect users.

========================================================================
ANALYTICS FEATURE
========================================================================

Track:
- clicks
- geography
- devices
- browsers

========================================================================
IMPLEMENTATION
========================================================================

Use asynchronous event processing.

========================================================================
FLOW
========================================================================

Redirect occurs
   ↓
Analytics event pushed to Kafka
   ↓
Background processing

========================================================================
WHY ASYNCHRONOUS?
========================================================================

Analytics should NOT slow redirects.

========================================================================
URL EXPIRATION
========================================================================

Some short URLs expire automatically.

========================================================================

Useful for:
temporary sharing.

========================================================================
SECURITY CONSIDERATIONS
========================================================================

1. Spam URLs
2. Malicious links
3. Phishing attacks

========================================================================
SOLUTIONS
========================================================================

1. URL validation
2. Rate limiting
3. Abuse detection

========================================================================
HOT URL PROBLEM
========================================================================

Some URLs become extremely popular.

========================================================================

Massive traffic spike occurs.

========================================================================
SOLUTION
========================================================================

Aggressive caching + CDN.

========================================================================
IMPORTANT SYSTEM DESIGN UNDERSTANDING
========================================================================

URL Shortener mainly becomes:
read-heavy distributed cache system.

========================================================================
DATABASE CHOICE
========================================================================

SQL or NoSQL both possible.

========================================================================
COMMON CHOICE
========================================================================

SQL often sufficient initially.

========================================================================

Later:
distributed NoSQL possible.

========================================================================
REAL WORLD ARCHITECTURE
========================================================================

Users
   ↓
CDN
   ↓
Load Balancer
   ↓
Application Servers
   ↓
Redis Cache
   ↓
Database Cluster

========================================================================
BOTTLENECKS
========================================================================

1. Database reads
2. Hot URLs
3. Cache misses
4. Unique ID generation

========================================================================
FAILURE HANDLING
========================================================================

1. Database replication
2. Cache replication
3. Multiple app servers
4. Retry mechanisms

========================================================================
IMPORTANT TRADE-OFFS
========================================================================

Strong consistency
vs
high availability.

========================================================================

Usually eventual consistency acceptable.

========================================================================
INTERVIEW TIPS
========================================================================

ALWAYS discuss:
- scaling
- caching
- database design
- bottlenecks
- failure handling

========================================================================
IMPORTANT INTERVIEW QUESTIONS
========================================================================

1. How generate short URLs?

→ Base62 encoded unique IDs.

========================================================================

2. Why Base62 used?

→ Compact URL representation.

========================================================================

3. Why caching important?

→ Fast redirection and lower DB load.

========================================================================

4. Why read-heavy system?

→ Redirect requests dominate.

========================================================================

5. What is collision?

→ Same short code for different URLs.

========================================================================

6. How handle hot URLs?

→ Aggressive caching and CDN.

========================================================================

7. Why asynchronous analytics?

→ Avoid slowing redirects.

========================================================================

8. Which database suitable?

→ SQL initially, distributed DB later.

========================================================================

9. Why replication important?

→ Availability and read scaling.

========================================================================
MOST IMPORTANT INTERVIEW LINE
========================================================================

URL shortener systems generate compact
unique identifiers
mapped to original URLs
while using caching,
replication,
and distributed scaling
for low-latency high-traffic redirection.

========================================================================

*/