/*

========================================================================
            CONSISTENCY vs AVAILABILITY
========================================================================

Consistency and Availability are
MOST IMPORTANT concepts
in distributed systems.

========================================================================

These concepts become critical
when systems run on:
multiple servers/databases.

========================================================================
MAIN QUESTION
========================================================================

Suppose:
multiple database servers exist.

========================================================================

How all servers maintain:
correct and updated data?

========================================================================

And what happens during:
network failures?

========================================================================

This leads to:
- Consistency
- Availability
- CAP Theorem

========================================================================
WHAT IS CONSISTENCY?
========================================================================

Consistency means:

All nodes see same data
at same time.

========================================================================
SIMPLE IDEA
========================================================================

Every server should return:
same latest value.

========================================================================
EXAMPLE
========================================================================

Suppose:
bank balance updated to ₹5000.

========================================================================

After update:
all database replicas should show:

        ₹5000

========================================================================

NOT:
- Server-1 → ₹5000
- Server-2 → ₹3000

========================================================================

Otherwise:
data inconsistency occurs.

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Consistency ensures:
data correctness.

========================================================================
STRONG CONSISTENCY
========================================================================

Every read gets:
latest updated value immediately.

========================================================================
EXAMPLE
========================================================================

Money transfer systems.

========================================================================

Banking systems require:
strong consistency.

========================================================================
WHY?
========================================================================

Wrong balance display
can create serious problems.

========================================================================
PROBLEM WITH STRONG CONSISTENCY
========================================================================

Requires synchronization
across multiple servers.

========================================================================

This increases:
- latency
- complexity
- coordination overhead

========================================================================
WHAT IS AVAILABILITY?
========================================================================

Availability means:

System always responds to requests.

========================================================================

Even during failures.

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Response may contain:
old/stale data.

========================================================================

But system should still respond.

========================================================================
EXAMPLE
========================================================================

Instagram like count.

========================================================================

Slight delay acceptable.

========================================================================

Users prefer:
system available
rather than perfect synchronization.

========================================================================
HIGH AVAILABILITY
========================================================================

System remains operational
almost all the time.

========================================================================
EXAMPLE
========================================================================

99.99% uptime.

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Modern internet systems prioritize:
high availability.

========================================================================
WHAT IS NETWORK PARTITION?
========================================================================

Very important distributed systems concept.

========================================================================

Network partition means:
servers cannot communicate properly.

========================================================================
EXAMPLE
========================================================================

Database Server-1
cannot communicate with:
Database Server-2

========================================================================

Due to:
- network failure
- timeout
- cable issue
- cloud outage

========================================================================
THIS CREATES
========================================================================

Partition Tolerance problem.

========================================================================
CAP THEOREM
========================================================================

Most famous theorem
in distributed systems.

========================================================================

CAP stands for:
- Consistency
- Availability
- Partition Tolerance

========================================================================
1. CONSISTENCY (C)
========================================================================

All nodes return same latest data.

========================================================================
2. AVAILABILITY (A)
========================================================================

System always responds.

========================================================================
3. PARTITION TOLERANCE (P)
========================================================================

System continues operating
despite network failures.

========================================================================
MOST IMPORTANT UNDERSTANDING
========================================================================

In distributed systems:

You cannot fully guarantee:
all three simultaneously.

========================================================================

During network partition,
system must choose between:
- Consistency
OR
- Availability

========================================================================
THIS IS CORE IDEA OF CAP THEOREM
========================================================================

========================================================================
EXAMPLE SCENARIO
========================================================================

Suppose:
two database replicas exist.

========================================================================

Replica-1 updated:
Balance = ₹5000

========================================================================

Network partition occurs.

========================================================================

Replica-2 still has:
Balance = ₹3000

========================================================================
NOW SYSTEM MUST CHOOSE
========================================================================

OPTION 1:
Return old value ₹3000

========================================================================

System remains:
AVAILABLE

========================================================================

But NOT consistent.

========================================================================
OPTION 2:
Reject request until synchronization.

========================================================================

System remains:
CONSISTENT

========================================================================

But NOT available.

========================================================================
CP SYSTEMS
========================================================================

Consistency + Partition Tolerance

========================================================================

Availability sacrificed during partition.

========================================================================
EXAMPLES
========================================================================

- Banking systems
- Financial systems

========================================================================
WHY?
========================================================================

Correctness more important
than availability.

========================================================================
AP SYSTEMS
========================================================================

Availability + Partition Tolerance

========================================================================

Consistency sacrificed temporarily.

========================================================================
EXAMPLES
========================================================================

- Social media
- Streaming systems

========================================================================
WHY?
========================================================================

Availability more important.

========================================================================

Users tolerate slightly stale data.

========================================================================
IMPORTANT UNDERSTANDING
========================================================================

Partition Tolerance usually mandatory
in distributed systems.

========================================================================

Because network failures are unavoidable.

========================================================================

So practical systems usually choose:
- CP
OR
- AP

========================================================================
EVENTUAL CONSISTENCY
========================================================================

Very important modern concept.

========================================================================

Data may not become consistent immediately.

========================================================================

Eventually all nodes synchronize.

========================================================================
EXAMPLE
========================================================================

Instagram likes update after few seconds.

========================================================================

Initially:
different users may see different counts.

========================================================================

Eventually:
all become same.

========================================================================
ADVANTAGE
========================================================================

Improves:
- scalability
- availability
- performance

========================================================================
DISADVANTAGE
========================================================================

Temporary stale data possible.

========================================================================
STRONG CONSISTENCY vs EVENTUAL CONSISTENCY
========================================================================

STRONG CONSISTENCY
-------------------
Immediate synchronization

========================================================================

EVENTUAL CONSISTENCY
---------------------
Synchronization after some delay

========================================================================
WHEN TO USE STRONG CONSISTENCY?
========================================================================

Use when correctness critical.

========================================================================
EXAMPLES
========================================================================

- Banking
- Payments
- Inventory systems

========================================================================
WHEN TO USE EVENTUAL CONSISTENCY?
========================================================================

Use when availability and scalability
more important.

========================================================================
EXAMPLES
========================================================================

- Social media
- Analytics
- Recommendation systems

========================================================================
REPLICATION AND CONSISTENCY
========================================================================

Replication creates:
multiple copies of data.

========================================================================

Challenge:
keeping replicas synchronized.

========================================================================
SYNCHRONOUS REPLICATION
========================================================================

Data written to all replicas immediately.

========================================================================

Provides:
strong consistency.

========================================================================

But slower.

========================================================================
ASYNCHRONOUS REPLICATION
========================================================================

Primary updates first.

========================================================================

Replicas update later.

========================================================================

Provides:
better performance and availability.

========================================================================

But temporary inconsistency possible.

========================================================================
IMPORTANT SYSTEM DESIGN UNDERSTANDING
========================================================================

Consistency decisions depend on:
business requirements.

========================================================================
EXAMPLE
========================================================================

Wrong Netflix recommendation:
acceptable.

========================================================================

Wrong bank balance:
NOT acceptable.

========================================================================
REAL WORLD EXAMPLES
========================================================================

BANKING SYSTEM
---------------
Prioritizes:
Consistency

========================================================================

INSTAGRAM
-----------
Prioritizes:
Availability

========================================================================

WHATSAPP
----------
Uses mixed strategies
depending on feature.

========================================================================
IMPORTANT INTERVIEW QUESTIONS
========================================================================

1. What is consistency?

→ All nodes see same latest data.

========================================================================

2. What is availability?

→ System always responds to requests.

========================================================================

3. What is network partition?

→ Servers unable to communicate.

========================================================================

4. What is CAP theorem?

→ Distributed systems cannot fully guarantee
Consistency,
Availability,
and Partition Tolerance simultaneously.

========================================================================

5. Difference between strong and eventual consistency?

Strong
--------
immediate synchronization

Eventual
----------
delayed synchronization

========================================================================

6. Why eventual consistency popular?

→ Better scalability and availability.

========================================================================

7. Which systems prefer strong consistency?

→ Banking and financial systems.

========================================================================

8. Which systems prefer availability?

→ Social media systems.

========================================================================

9. What is replication?

→ Copying data across multiple servers.

========================================================================
MOST IMPORTANT INTERVIEW LINE
========================================================================

Distributed systems must balance
consistency,
availability,
and partition tolerance
based on business requirements.

========================================================================

*/