////package java_recap.springboot.annotations.performance;
//
///*
//
//=================================================================
//        SPRING SCHEDULING ANNOTATIONS
//=================================================================
//
//Annotations:
//-------------
//@EnableScheduling
//@Scheduled
//
//Core Concepts:
//---------------
//1. Task Scheduling
//2. Cron Jobs
//3. Background Automation
//4. Fixed Delay
//5. Fixed Rate
//6. Scheduler Threads
//7. Periodic Execution
//8. Thread Management
//
//=================================================================
//                WHY SCHEDULING EXISTS?
//=================================================================
//
//Many enterprise tasks must run automatically.
//
//=================================================================
//                EXAMPLES
//=================================================================
//
//1. Daily reports
//2. Cleanup old data
//3. Payment retries
//4. Email reminders
//5. Cache refresh
//6. Stock updates
//
//=================================================================
//                PROBLEM
//=================================================================
//
//Without scheduling:
//--------------------
//
//Developers manually trigger tasks.
//
//=================================================================
//                RESULT
//=================================================================
//
//1. Human dependency
//2. Operational issues
//3. Missed executions
//4. Poor automation
//
//=================================================================
//                SOLUTION
//=================================================================
//
//Task Scheduling.
//
//=================================================================
//                WHAT IS SCHEDULING?
//=================================================================
//
//Executing tasks automatically
//at specific intervals/times.
//
//=================================================================
//                SIMPLE MEANING
//=================================================================
//
//"Run this method automatically later."
//
//=================================================================
//                1. @EnableScheduling
//=================================================================
//
//Main annotation enabling scheduling.
//
//=================================================================
//                EXAMPLE
//=================================================================
//
//@Configuration
//@EnableScheduling
//public class SchedulerConfig {
//
//}
//
//=================================================================
//                WHAT HAPPENS?
//=================================================================
//
//Spring enables:
//----------------
//
//1. Scheduler infrastructure
//2. Background scheduler threads
//3. Periodic task execution
//
//=================================================================
//                INTERNAL WORKING
//=================================================================
//
//Application Starts
//        ↓
//Spring scans @Scheduled methods
//        ↓
//TaskScheduler initialized
//        ↓
//Scheduler threads created
//        ↓
//Methods executed periodically
//
//=================================================================
//                IMPORTANT
//=================================================================
//
//Spring scheduling internally uses:
//-----------------------------------
//
//TaskScheduler + Thread Pools
//
//=================================================================
//                2. @Scheduled
//=================================================================
//
//Most important scheduling annotation.
//
//=================================================================
//                DEFINITION
//=================================================================
//
//@Scheduled tells Spring:
//
//"Execute this method automatically."
//
//=================================================================
//                EXAMPLE
//=================================================================
//
//@Scheduled(fixedRate = 5000)
//
//public void generateReport() {
//
//}
//
//=================================================================
//                WHAT HAPPENS?
//=================================================================
//
//Method executes every 5 seconds.
//
//=================================================================
//                TYPES OF SCHEDULING
//=================================================================
//
//1. fixedRate
//2. fixedDelay
//3. cron
//
//=================================================================
//                1. fixedRate
//=================================================================
//
//Runs task at fixed interval.
//
//=================================================================
//                EXAMPLE
//=================================================================
//
//@Scheduled(fixedRate = 5000)
//
//=================================================================
//                FLOW
//=================================================================
//
//Start Task
//    ↓
//Every 5 seconds start again
//
//=================================================================
//                IMPORTANT
//=================================================================
//
//Next execution time measured
//from START of previous execution.
//
//=================================================================
//                EXAMPLE TIMELINE
//=================================================================
//
//0 sec → start
//5 sec → start again
//10 sec → start again
//
//=================================================================
//                PROBLEM
//=================================================================
//
//If task takes longer:
//----------------------
//
//Multiple executions may overlap.
//
//=================================================================
//                REAL PROJECT USE CASE
//=================================================================
//
//1. Metrics collection
//2. Monitoring systems
//3. Heartbeat signals
//
//=================================================================
//                2. fixedDelay
//=================================================================
//
//Runs task after previous execution completes.
//
//=================================================================
//                EXAMPLE
//=================================================================
//
//@Scheduled(fixedDelay = 5000)
//
//=================================================================
//                FLOW
//=================================================================
//
//Task completes
//    ↓
//Wait 5 seconds
//    ↓
//Run again
//
//=================================================================
//                IMPORTANT
//=================================================================
//
//Delay measured AFTER completion.
//
//=================================================================
//                EXAMPLE TIMELINE
//=================================================================
//
//0 sec → start
//3 sec → finish
//8 sec → next start
//
//=================================================================
//                BENEFIT
//=================================================================
//
//Avoid overlapping executions.
//
//=================================================================
//                REAL PROJECT USE CASE
//=================================================================
//
//1. File processing
//2. Batch jobs
//3. Sequential processing
//
//=================================================================
//                3. cron
//=================================================================
//
//Most powerful scheduling type.
//
//=================================================================
//                DEFINITION
//=================================================================
//
//Cron expression defines exact execution schedule.
//
//=================================================================
//                EXAMPLE
//=================================================================
//
//@Scheduled(cron = "0 0 12 * * *")
//
//=================================================================
//                MEANING
//=================================================================
//
//Run every day at 12 PM.
//
//=================================================================
//                CRON FORMAT
//=================================================================
//
//second
//minute
//hour
//day-of-month
//month
//day-of-week
//
//=================================================================
//                EXAMPLE
//=================================================================
//
//0 0 0 * * *
//
//=================================================================
//                MEANING
//=================================================================
//
//Every midnight.
//
//=================================================================
//                COMMON CRON EXAMPLES
//=================================================================
//
//Every 5 minutes:
//-----------------
//
//0 */5 * * * *
//
////Every day at 2 AM:
////        -------------------
////
////        0 0 2 * * *
////
////Every Monday:
////        --------------
////
////        0 0 10 * * MON
////
////=================================================================
////REAL PROJECT USE CASES
////=================================================================
////
////        1. Daily backups
////2. Monthly reports
////3. Subscription renewal
////4. Data cleanup
////5. Analytics jobs
////
////=================================================================
////INITIAL DELAY
////=================================================================
////
////@Scheduled(
////        initialDelay = 10000,
////        fixedRate = 5000
////)
////
////=================================================================
////MEANING
////=================================================================
////
////Wait 10 seconds before first execution.
////
////        =================================================================
////SCHEDULER THREADS
////=================================================================
////
////Tasks executed using background threads.
////
////=================================================================
////DEFAULT BEHAVIOR
////=================================================================
////
////Single-threaded scheduler.
////
////        =================================================================
////PROBLEM
////=================================================================
////
////Long-running task blocks others.
////
////        =================================================================
////SOLUTION
////=================================================================
////
////Custom TaskScheduler thread pool.
////
////        =================================================================
////CUSTOM THREAD POOL
////=================================================================
////
////@Bean
////public TaskScheduler scheduler() {
////
////    ThreadPoolTaskScheduler scheduler =
////            new ThreadPoolTaskScheduler();
////
////    scheduler.setPoolSize(5);
////
////    return scheduler;
////
////}
////
////=================================================================
////WHY IMPORTANT?
////        =================================================================
////
////Multiple scheduled tasks
////can run concurrently.
////
////=================================================================
////SCHEDULING FLOW
////=================================================================
////
////Application Starts
////    ↓
////Scheduler scans tasks
////    ↓
////Scheduler thread waits
////    ↓
////Trigger time reached
////    ↓
////Method executes
////
////=================================================================
////COMMON REAL PROJECT TASKS
////=================================================================
////
////E-commerce:
////        ------------
////
////        1. Order cleanup
////2. Coupon expiry
////
////Banking:
////        ---------
////
////        1. Interest calculation
////2. Settlement jobs
////
////Gaming:
////        --------
////
////        1. Leaderboard refresh
////2. Reward distribution
////
////Microservices:
////        ---------------
////
////        1. Retry failed events
////2. Cache synchronization
////
////=================================================================
////IMPORTANT LIMITATION
////=================================================================
////
////Same self-invocation proxy issue exists.
////
////        =================================================================
////WHY?
////        =================================================================
////
////Scheduling also uses Spring proxies internally.
////
////        =================================================================
////LONG RUNNING TASK PROBLEM
////=================================================================
////
////Task duration > interval.
////
////=================================================================
////RESULT
////=================================================================
////
////Thread exhaustion
////or overlapping execution.
////
////=================================================================
////SOLUTION
////=================================================================
////
////        1. Proper thread pools
////2. Distributed schedulers
////3. Queue-based systems
////
////=================================================================
////DISTRIBUTED SYSTEM PROBLEM
////=================================================================
////
////Multiple servers running same scheduler.
////
////=================================================================
////RESULT
////=================================================================
////
////Duplicate execution.
////
////        =================================================================
////EXAMPLE
////=================================================================
////
////        3 servers
////→ same cron job executes 3 times
////
////=================================================================
////SOLUTION
////=================================================================
////
////        1. Distributed lock
////2. Quartz Scheduler
////3. ShedLock
////
////=================================================================
////INTERVIEW IMPORTANT
////=================================================================
////
////Q:
////        ---
////Difference between fixedRate
////and fixedDelay?
////
////        =================================================================
////ANSWER
////=================================================================
////
////fixedRate:
////        ------------
////Measured from task START.
////
////fixedDelay:
////        -------------
////Measured from task COMPLETION.
////
////        =================================================================
////ANOTHER IMPORTANT QUESTION
////=================================================================
////
////Q:
////        ---
////How @Scheduled works internally?
////
////        =================================================================
////ANSWER
////=================================================================
////
////Spring creates scheduler infrastructure,
////registers scheduled methods,
////and TaskScheduler executes methods
////using background threads periodically.
////
////        =================================================================
////ANOTHER IMPORTANT QUESTION
////=================================================================
////
////Q:
////        ---
////Why custom scheduler thread pool needed?
////
////        =================================================================
////ANSWER
////=================================================================
////
////Default scheduler is single-threaded.
////        Long-running tasks can block others.
////Thread pools improve concurrency.
////
////        =================================================================
////TRICKY INTERVIEW QUESTION
////=================================================================
////
////Q:
////        ---
////Can @Scheduled run in distributed systems safely?
////
////NOT automatically.
////
////        =================================================================
////WHY?
////        =================================================================
////
////Each server executes same scheduled task.
////
////        =================================================================
////SOLUTION
////=================================================================
////
////Distributed locking mechanisms.
////
////=================================================================
////ANOTHER TRICKY QUESTION
////=================================================================
////
////Q:
////        ---
////Can scheduled method be private?
////
////NO usually.
////
////Proxy interception limitations.
////
////=================================================================
////COMMON MISTAKES
////=================================================================
////
////        1. Long-running scheduled tasks
////
////2. No distributed locking
////
////3. Blocking operations
////
////4. Ignoring thread pools
////
////5. Wrong cron expressions
////
////=================================================================
////BEST PRACTICE
////=================================================================
////
////        1. Keep scheduled tasks lightweight
////2. Use distributed locks
////3. Configure scheduler thread pools
////4. Monitor execution times
////5. Avoid heavy DB work in schedulers
////
////=================================================================
////REAL INTERVIEW ANSWER
////=================================================================
////
////Q. Explain complete flow of @Scheduled.
////
////        Answer:
////        --------
////        1. Application starts
////2. Spring scans @Scheduled methods
////3. TaskScheduler registers tasks
////4. Scheduler thread waits for trigger
////5. Method executes automatically
////6. Task repeats based on configuration
////
////=================================================================
////SUMMARY
////=================================================================
////
////@EnableScheduling
////------------------
////Enables scheduling infrastructure.
////
////@Scheduled
////------------
////Executes methods automatically.
////
////        Supports:
////        -----------
////
////        1. fixedRate
////2. fixedDelay
////3. cron expressions
////
////Spring internally uses:
////        ------------------------
////
////        1. TaskScheduler
////2. Background threads
////3. Thread pools
////4. Proxy infrastructure
////
////=================================================================
////
////        */
